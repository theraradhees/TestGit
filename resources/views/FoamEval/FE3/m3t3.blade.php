    <h2>Test Method</h2>
    <ol type="a">
        <li>Test Method A (Refractometer or Conductivity Meter) </li>
        <ol>
            <li>Foam Sampling Apparatus </li>
            <li>The foam sample collector shall be constructed as specified in Figure 3-3. </li>
            <p style="text-align: center"><b>Figure 3-3. Foam Sample Collector Construction </b></p>
            <li>The foam sample shall be collected in a standard 1000-mL-capacity graduated cylinder. The cylinder shall be cut off at the 1000-mL mark to ensure a fixed volume of foam as a sample. The cylinder shall be marked in 10-mL graduations below the 100-mL mark. </li>
        </ol>
        <p></p>
        <li>Test Procedure </li>
        <ol>
            <li>The empty weight of the foam sample container shall be recorded to the nearest gram on a balance having a maximum capacity sufficient to weigh the foam sample container and the foam sample. The foam sample collector shall then be located in the center of the discharge pattern determined in Section 6.5. The foam sample container shall be positioned at the bottom of the foam collector so that the foam hitting the collector flows into the container. The foam nozzle shall be aimed so that the foam deflects off the side of the foam collector, adjusted to its normal operating pressure, and then moved so as to discharge foam onto the foam sample collector. As soon as the foam sample container has been completely filled with foam, the discharge nozzle shall be shut off and the timing of the 25 percent drainage started. </li>
            <li>The foam sample container shall be removed from the base of the foam collector, excess foam struck off the top of the foam container using a straight edge, and any remaining foam wiped from the outside surface of the container. The container shall then be placed on the balance. The total weight of the foam sample and container shall be determined to the nearest gram. The weight of the foam sample in the container shall be determined by subtracting the weight of the empty container from the weight of the container filled with the foam. The weight of the foam sample in grams shall be divided by 4 to obtain the equivalent 25 percent drainage volume in milliliters. </li>
            <li>The foam sample container shall be placed on a level surface at a convenient height. At 30-second intervals, the level of accumulated solution in the bottom of the cylinder shall be noted and recorded. The drainage time versus the volume relationship shall be recorded until the 25 percent volume has been exceeded. The 25 percent drainage time shall then be interpolated from the data.</li>
