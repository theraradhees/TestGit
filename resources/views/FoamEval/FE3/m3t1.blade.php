    <h2>Method A (Refractometer or Conductivity Meter)</h2>
    <p>
        A calibration curve shall be prepared using the following apparatus and procedure. 
    </p>
    <ol type="a">
        <li>apparatus</li>
        <p>In preparing the calibration curve, the following apparatus shall be used: </p>
        <ol>
            <li>Three 100-mL graduates </li>
            <li>One measuring pipette (10-ml capacity) </li>
            <li>One 100-mL breaker </li>
            <li>One 500-mL breaker </li>
        </ol>
        <p></p>
        <li>Procedure </li>
        <p>
            Using water and foam concentrate from the tanks of the vehicle to be tested, three standard solutions shall be made up by pipe fitting, into three 100-mL graduated cylinders, volumes of foam concentrate in milliliters equal to the following: 
        </p>
        <ol>
            <li>The nominal concentration of the foam concentrate. </li>
            <li>One-third more than the nominal concentration of the foam concentrate. </li>
            <li>One-third less than the nominal concentration of the foam concentrate. </li>
        </ol>
        <p>
            The graduated cylinders shall then be filled to the 100-mL mark with water. After thoroughly mixing, a refractive index reading shall be taken of each standard. A plot shall be made on graph paper of the scale reading against the known foam solution concentrates and shall serve as a calibration curve for this particular series of foam tests. 
        </p>
        <p></p>
        <li>Test Sample Concentration </li>
        <p>
            Portions of solution drained during the drainage test described in 2.2.2 point 9) in module 2. shall be used as a source of test sample for the concentration determination. Refractive index or conductivity readings of the test sample shall be compared to the calibration curve and the corresponding foam solution concentration read from the graph.
        </p>
    </ol>
    <br>
