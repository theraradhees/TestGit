    <h1>ARFF Vehicle Foam Production Performance Test</h1>
    <hr>
    <p>
        It is essential that the foam produced by an RFF vehicle, or other such appliance, is of an acceptable quality and the delivery parameters such as monitor jet range and pattern meet and are maintained to the appropriate operational requirement. 
    </p>
    <p>
        The Foam Production Performance Test should confirm the following: 
    </p>
    <ol type="a">
        <li>The induction percentage for all foam-making devices. </li>
        <li>The expansion ratio from all foam making devices.</li>
        <li>The quarter drainage time from all foam making devices. </li>
        <li>The jet range of the main monitor. </li>
        <li>The spray pattern of the main monitor </li>
    </ol>
    <p>
        ICAO recommends that the ARFF Vehicle Foam Production Performance Test should be conducted; 
    </p>
    <ol type="a">
        <li>once a year (annually)</li>
        <li>When a RFF vehicle is first acquired by the licence holder for operational use at an aerodrome. </li>
        <li>When significant maintenance, refurbishment or component replacement has been undertaken on a RFF vehicle that could affect a change in the foam quality or production performance of the foam-making system. </li>
    </ol>
    <p>
        Local regulation (KP 605 Th 2015) - semiannually, and the result should be reported to airport authority; 
    </p>
    <p>
        Anytime when necessary if any inspection/audit. 
    </p>
    <hr>

