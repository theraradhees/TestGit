    <h1>Baseline</h1>
    <hr>
    <h2>Baseline</h2>
    <p>
        n conducting ARFF Vehicle Foam Production Performance Test using a refractometer and conductivity-meter manufacture, baseline is required. 
    </p>
    <p>Baseline is used as a point of measuring the concentration of foam solution that will be compared with a sample of foam solution from the vehicle outlet. </p>
    <p>Baseline is necessary to make a number of water and foam concentrate taken from the vehicle tank. </p>
    <p>Samples of water and foam concentrate should be taken from the top of the tank. To prevent sediment and sewage sludge that are usually found at the bottom of the tank contaminating baseline. </p>

    <h2>Create a baseline </h2>
    <p>
        Take 4 pieces of pyrex measuring cup 100 ml to create a solution with the induction ratio, respectively 2%, 4%, 6%, 8% by: 
    </p>
    <ol>
        <li>Sample 2%: 98 ml of water + 2 ml foam concentrate = 100 ml solution. </li>
        <li>Sample 4%: 96 ml of water + 4 ml foam concentrate = 100 ml foam solution. </li>
        <li>Sample 6%: 94 ml of water + 6 ml foam concentrate = 100 ml foam solution. </li>
        <li>Sample 8%: 92 ml of water + 8 ml foam concentrate = 100 ml foam solution. </li>
    </ol>
    <p></p>

    <h2>Baseline measurement </h2>
    <ol type="a">
        <li>With refractometer </li>
        <ol>
            <li>Measure the refractive index scale of each BASELINE. Record the results. </li>
            <li>Enter its results into graphical form. </li>
            <li>Graphs should form a straight line.</li>
            <li>If not form a straight line, there is a manufacturing fault BASELINE.</li>
            <p>Repeat. </p>
        </ol>
        <li>With Conductivity Meter </li>
        <ol>
            <li>Measure the conductivity of the water tank of the vehicle. Record the results </li>
            <li>Measure the conductivity of each BASELINE. Record the results. </li>
            <li>Enter its results into graphical form. </li>
            <li>Graphs should form a straight line. </li>
            <li>If not form a straight line, there is a manufacturing fault BASELINE.</li>
            <p>Repeat. </p>
        </ol>
    </ol>
    <p style="text-align: center"><b>Figure 3-2. Conductivity </b></p>
    <br>
    <h2>Foam Solution Concentration Determination </h2>
    <p>
        Foam solution concentration shall be determined using one of the following methods as described in 3.1.1 or 3.1.2. When testing foam concentration from vehicles, the lowest flow rate outlet shall be tested first. One handheld refractometer or one handheld conductivity meter suitable for the foam concentrate being tested shall be used. Method B (in 3.1.2) shall be used only when using a refractometer. Refractive index readings shall be taken by placing a few drops of solution on the refractometer prism, closing the cover plate, and observing the scale reading at the dark field intersection. Or, if using conductivity, the probe shall be dipped into the sample and the digital scale read. 
    </p>
