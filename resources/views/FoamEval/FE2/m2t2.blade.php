    <hr>
    <h2>Foam and Foam System Tests </h2>
    <p>
        The expansion ratio and 25 percent drainage time are the foam characteristics that shall be determined. Additionally, the foam concentration shall be determined, both as a test of the vehicle proportioning system and to establish the legitimacy of the
        expansion and drainage data obtained. Foam distribution patterns shall be determined for use in calculating the area of fire that the vehicle is capable of controlling.
    </p>
    <br>
    <h2>Turret Nozzles, Ground Sweep Nozzle, and Hand Line Nozzles </h2>
    <ol>
        <li>Ground sweep nozzles and hand line foam nozzles shall be discharged onto a paved surface for a period of 30seconds. </li>
        <li>Ground sweep nozzles shall be discharged from their fixed positions. </li>
        <li>Hand line nozzles shall be held at their normal working height and tilted upward to form a 30 degree angle with die horizontal. </li>
        <li>Markers shall be set out to denote the outline of the effective foam pattern and plotted. </li>
        <li>Patterns from both the straight stream and the fully dispersed nozzle settings shall be established, measured, and recorded. </li>
        <li>Foam samples selected for analysis shall be representative of the foam produced by the nozzle as it would be applied to the fire hazard. This shall be accomplished by placing a foam sample collector in the center of the ground pattern as determined
            in the nozzle pattern test. </li>
        <li>Foam samples shall be analyzed for expansion and drainage time using the criteria specified in Section 2.2.4 of this module. </li>
        <li>Foam samples shall be analyzed for concentration as specified in Section 2.2.5 of this module. </li>

    </ol>
    <br>
    <h2>Understruck Nozzle </h2>
    <ol>
        <li>The foam distribution pattern shall provide protection for the area beneath the vehicle. </li>
        <li>Foam samples shall be collected and analyzed for concentration as specified in Section 2.2.5. </li>
    </ol>
    <br>
