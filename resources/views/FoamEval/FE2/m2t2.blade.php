    <h2>Foam Solution Concentration </h2>
    <ol type="a">
        <li>Foam solution concentration shall be determined using one of the following methods A (Refractometer or Conductivity meter) or method B (Refractometer only). When testing foam concentration from vehicles, the lowest flow rate outlet shall be tested
            first. One handheld refractometer or one handheld conductivity meter suitable for the foam concentrate being tested shall be used. Method B (Refractometer only) shall be used only when using a refractometer. Refractive index readings shall
            be taken by placing a few drops of solution on the refractometer prism, closing the cover plate, and observing the scale reading at the dark field intersection. Or, if using conductivity, the probe shall be dipped into the sample and the digital
            scale read. </li>
        <li>For nominal 6 percent concentrates, the concentration shall be between 5.5 percent and 7.0 percent for turret and ground sweep nozzles and between 5.5 percent and 8.0 percent for hand line and understuck nozzles. </li>
        <li>For nominal 3 percent concentrates, the concentration shall be between 2.8 percent and 3.5 percent for turret and ground sweep nozzles and between 2.8 percent and 4.0 percent for hand line and undertruck nozzles. </li>
    </ol>
