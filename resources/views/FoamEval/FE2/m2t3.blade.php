    <h1>Fire Test </h1>
    <hr>
    <h2>Personal Protective Equipment</h2>
    <p>
        Designated equipment may be necessary, and personnel involved should receive any necessary additional training and be provided with appropriate personal protective equipment.
    </p>
    <p>
        RFF personnel entering and working in an aircraft incident scene or impact area should be utilizing the appropriate personal protective equipment (PPE) and receive the appropriate level of decontamination immediately after their duties are completed. 
    </p>
    <p>
        The job nature of RFF personnel poses numerous potential hazards (Liveware vs. Environment). The risk of inhalation of carbon or smoke particles when extinguishing a fire, either during an incident or during training, is very high. Therefore, RFF services must provide all fire fighters with the appropriate personal protective equipment (PPE) such as self-containing breathing apparatus (SCBA), helmets, boots, protective clothing etc. In relation to day-to-day operations, the uniform worn by RFF personnel should also be of a suitable material depending on the local climate and conditions. 
    </p>
    <br>
    <h2>Fire Test Method </h2>
    <p>Objective: To evaluate the ability of a foam concentrate to: </p>
    <ol type="a">
        <li>Extinguish a fire of: </li>
        <p>
            2.8 m2, - performance Level A

        </p>
        <p> 4.5 m2, - performance Level B</p>

        <p> 7.3 m2, - performance Level C, as appropriate;</p>
        </p>
        <li>Resist burn back due to exposure to fuel and heat. </li>
    </ol>
    <p>Equipment</p>
    <ol type="a">
        <li>A circular fire steel tray of: </li>
        <p>2.8 m2, - performance Level A </p>
        <p>4.5 m2, - performance Level B </p>
        <p>7.32 m2, - performance Level C </p>
        <p>The vertical wall shall be 200 mm; </p>
        <li>Equipment or access to facilities to enable accurate recordings of: </li>
        <ol>
            <li>Air temperature; </li>
            <li>Water temperature; and </li>
            <li>Wind velocity; </li>
        </ol>
        <li>Fuel</li>
        <p>60 L of Avtur (Jet A1) for performance level A tests; </p>
        <p>100 L of Avtur (Jet A1) for performance level B tests; and </p>
        <p>157 L of Avtur (Jet A1) for performance level C tests </p>
        <p><i>Note 1. - Avtur Jet A or kerosene with similar specifications may be used if approved by the appropriate authority.</i></p>
        <p><i>Note 2. - As some aviation kerosene may contain additives, it is recommended that testing agencies utilise a pure kerosene test fuel in order to maintain and establish repeatable test results. </i></p>
        <li>Ranch pipe, straight stream, air aspirating nozzle; </li>
        <li>Suitable stop watch; </li>
        <li>Circular, burn back pot, measuring 300 mm (internal diameter), 200 mm high, 2 L of gasoline or kerosene; and </li>
        <li>Protective screen between tray and equipment, for protection against radiant heat, is acceptable. </li>
        <p>test Conditions:</p>
        <ol type="a">
            <li>Air temperature (EC) = 15C </li>
            <li>Air temperature (EC) = 15C </li>
            <li>Wind velocity (m/s) = 3 </li>
            <li>The test shall not be carried out in conditions of precipitation, if outdoors. </li>
        </ol>
        <p style="text-align: center"><b>The test shall not be carried out in conditions of precipitation, if outdoors. </b></p>
    </ol>
    <br>
    <h2>Fire Test Procedures </h2>
    <ol type="a">
        <li> Position the chamber holding the premix foam upwind of the fire with the nozzle horizontal at a height of 1 m above the upper edge of the tray and at a distance that will ensure that the foam will fall into the centre of the tray. </li>
        <p></p>
        <li>Test the foam apparatus to ensure: </li>
        <ol>
            <li>nozzle pressure; and </li>
            <li>discharge rate </li>
        </ol>
        <p></p>
        <li>Different fuel tray will be used in such condition: </li>
        <ol>
            <li>When testing performance level A foam, place 60 L of water and 60 L of fuel into a 2.8 m2 tray. </li>
            <li>When testing performance level B foam, place 100 L of water and 100 L of fuel into a 4.5 m2 tray. </li>
            <li>When testing performance level C foam, place 157 L of water and 157 L of fuel into a 7.32 m2 tray. </li>
        </ol>
        <p></p>
        <li>Position the protective screen, if required. </li>
        <ol>
            <li value="4">Test the foam apparatus to ensure a nozzle pressure of approximately 7 bar and a discharge rate of 11.4 l/min. </li>
            <li>Record the air, kerosene, water and foam premix temperature and check it is in the correct range. </li>
            <li>Record the wind velocity and check it is in the correct range. </li>
            <li>Ignite fuel and allow 60 seconds preburn from full involvement. </li>
            <p><i>Note 1. - full involvement shall be obtained in less than 30 seconds after the beginning of ignition </i></p>
            <p><i>Note 2. - ignition method shall forbid putting solid or liquid substance into the kerosene, for example ignition with a gas burner is acceptable. </i></p>
            <li>Apply foam continuously while maintaining the nozzle pressure and an application rate of 11.4 l/min for 120 seconds. </li>
            <li>Record extinction time.</li>
            <li>Place burn back pot in centre of fire tray. </li>
            <li>Ignite burn back pot 120 seconds after end of application of foam. </li>
            <li>Record when 25 percent of the fuel area is re-involved with fire. </li>
        </ol>
    </ol>
    <br>
    <h2>Result</h2>
    <p>Compare the Result to the Standard, with following parameters: </p>
    <ol type="a">
        <li>All fires in the fuel tray should be extinguished ≤ 60 seconds after the foam emitted; </li>
        <li>Ignition of 25% of the fuel tray after burnback pot ignited should be ≥ 5 minutes. </li>
    </ol>
    <p>
        Minute flames (flickers) visible between the foam blanket and the inner edge of the tray are acceptable: 
    </p>
    <ol type="a">
        <li>If they don’t spread in a cumulative length exceeding 25% of the circumference of the inner edge of the tray and </li>
        <li>They are totally extinguished during the second minute of foam application. </li>
    </ol>
