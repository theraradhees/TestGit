    <p>
        For each performance level, a foam concentrate is acceptable:
    </p>
    <ol>
        <li> If the time to extinguish the fire from the overall surface of the tray is equal or less than 60 seconds and;</li>
        <li> If the time to extinguish the fire from the overall surface of the tray is equal or less than 60 seconds and;</li>
    </ol>
    <p>
        Note for testing authorities: At the 60 seconds time, minute flames (flickers) visible between the foam blanket and the inner edge of the tray are acceptable:
    </p>
    <ol>
        <li>If they don’t spread in a cumulative length exceeding 25% of the circumference of the inner edge of the tray and; </li>
        <li>They are totally extinguished during the second minute of foam application. </li>
    </ol>
    <br>
    <p>
        The level of protection to be provided at an airport should be based on the dimensions of the aeroplanes normally using the airport as adjusted for their frequency of operations.
    </p>
    <p>
        The airport category for RFF should be based on the over-all length of the longest aeroplanes normally using the airport and their maximum fuselage width. The airport category should be determined from Table 2-1 by categorizing the aeroplanes using the
        airport, by first evaluating their over-all length and second, their fuselage width. If after selecting the category appropriate to an aeroplane’s over-all length that aeroplane’s fuselage width is greater than the maximum width in column 3 for
        that category, then the category for that aeroplane is actually one category higher.
    </p>
