  <h1>Introduction</h1>
  <hr>
  <p>
  For the most part, foam concentrates stored under satisfactory storage conditions and manufactured by reputable companies such as <i>Buckeye Fire Equipment Company</i> should provide years of trouble-free service. 
  </p>
  <p>
    However, even under the most rigid of condition and even with the best choice of foam concentrates, deterioration could occur. Degradation of the foam concentrate could happen due to a number of circumstances and as such periodic maintenance of the foam concentrate is warranted. 
  </p>
  <p>
    At least annually, samples of foam concentrate should be taken and analyzed as specified in NFPA Pamphlet II Standard for Low Expansion Foam, to verify their continued use and to check for the following conditions: 
  </p>
  <ol type="a">
    <li>To check the foam concentrate for an indication of dilution or evaporation.</li>
    <li>To check the foam concentrate for the effect of freezing. To check the foam concentrate for the possible effects of elevated temperatures. </li>
    <li>To check that the container material is suitable for long-term foam concentrate storage. </li>
    <li>To check that the foam concentrate has not been contaminated with other inferior or incompatible foam concentrates. </li>
    <li>To check that the foam concentrate has not been affected by unsuitable storage conditions.</li>
  </ol>
  <p>
    Before reviewing the merits of the different types of foam concentrates, there are certain terminologies associated with foam that must be understood. 
  </p>
  <p>
    <b>Foam :</b> A fire fighting foam is simply a stable mass of small air-field bubbles, which have a lower density than oil, gasoline or water. Foam is made up of three ingredients â€“ water, foam concentrate and air. When mixed in the correct proportions, these three ingredients form a homogeneous foam blanket. 
  </p>
  <p>
    <b>Foam Solution :</b> This is a solution of water and foam concentrate after they have been mixed together in the correct proportions 
  </p>
  <p>
    <b>Foam Concentrate :</b> This liquid concentrate is supplied from the manufacturer which when mixed with water in the correct proportion forms a foam solution. 
  </p>



