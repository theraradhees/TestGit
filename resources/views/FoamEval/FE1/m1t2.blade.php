    <h2>Foam viscosity</h2>
    <p>
        Viscosity is a measure of how well a liquid will flow. Liquids are generally classed as either being non-Newtonian or Newtonian. A low viscosity is often desirable because it improves the flow characteristics of a foam concentrate through pick up tubes, pipework and induction equipment. The viscosity of the foam concentrate is usually measured either at 20DC or at its minimum use temperature 
    </p>
    <ol type="A">
        <li>Preparation</li>
        <ol type="a">
            <li>Prepare Viscosity Test Equipment</li>
            <ul>
                <li>Viscometer Ostwald </li>
                <li>stopwatch </li>
            </ul>
            <li>Prepare Test Record Form </li>
            <li>Prepare Foam concentrate and Water to be tested </li>

        </ol>
        <li>Testing Procedures</li>
        <ol>
            <li>Put foam concentrate on the part that does not narrow tube; </li>
            <li>Incoming liquid foam will meet bubbling area; </li>
            <li>After a full-on part of the bubbly liquid will rise into the narrowed area, when the liquid touches on the areas marked green then a timing device (stop watch) is executed; </li>
            <li>Time permitted 1 seconds to reach the marked green on top; </li>
            <li>If the time it takes less than 1 second, then the foam concentrate too watery; </li>
            <li>If the time is more than 1 second then the foam concentrate too viscous. </li>
