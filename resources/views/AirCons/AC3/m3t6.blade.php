    <h1>Failure of Equipment</h1>
    <hr>
    <h2>
        Aircraft Radio Transmitter Failure
    </h2>
    <ol type="a">
        <li>If two-way communication is lost with an aircraft, the controller should determine whether or not the aircraft’s receiver is functioning by instructing the aircraft on the channel so far used to acknowledge by making a specified manoeuvre and by observing the aircraft’s track, or by instructing the aircraft to operate IDENT or to make SSR code and/or ADS-B transmission changes.</li>
        <p>
            Note 1.— Transponder-equipped aircraft experiencing radio communication failure will operate the transponder on Mode A Code 7600.
        </p>
        <p>
            Note 2.— ADS-B-equipped aircraft experiencing radio communication failure may transmit the appropriate ADS-B emergency and/or urgency mode.
        </p>
        <li>If the action prescribed in 2.1.1 is unsuccessful, it shall be repeated on any other available channel on which it is believed that the aircraft might be listening.</li><p></p>
        <li>In both the cases covered by 2.1.1 and 2.1.2, any manoeuvreing instructions shall be such that the aircraft would regain its current cleared track after having complied with the instructions received.</li><p></p>
        <li>Where it has been established by the action in 2.1.1 that the aircraft’s radio receiver is functioning, continued control can be effected using SSR code/ADS-B transmission changes or IDENT transmissions to obtain acknowledgement of clearances issued to the aircraft.</li>
    </ol>
    <br>
    <h2>Complete aircraft communication failure</h2>
    <ol type="a">
        <li>
            When a controlled aircraft experiencing complete communication failure is operating or expected to operate in an area and at flight levels where an ATS surveillance service is applied, separation specified minima based on ATS surveillance systems may continue to be used. However, if the aircraft experiencing the communication failure is not identified, separation shall be applied between
            n identified aircraft and all unidentified aircraft observed along the expected route of the aircraft with the communication failure, until such time as it is known, or can safely be assumed, that the aircraft with radiocommunication failure has passed through the airspace concerned, has landed, or has proceeded elsewhere.
        </li>
    </ol>
    <br>
    <h2>Aircraft transponder failure in areas where the carriage of a functioning transponder is mandatory</h2>
    <ol type="a">
        <li>When an aircraft experiencing transponder failure after departure is operating or expected to operate in an area where the carriage of a functioning transponder with specified capabilities is mandatory, the ATC units concerned should endeavour to provide for continuation of the flight to the aerodrome of first intended landing in accordance with the flight plan. However, in certain traffic situations, either in terminal areas or en-route, continuation of the flight may not be possible, particularly when failure is detected shortly after take-off. The aircraft may then be required to return to the departure aerodrome or to land at the nearest suitable aerodrome acceptable to the operator concerned and to ATC.</li><p></p>
        <li>In case of a transponder failure which is detected before departure from an aerodrome where it is not practicable to effect a repair, the aircraft concerned should be permitted to proceed, as directly as possible, to the nearest suitable aerodrome where repair can be made. When granting clearance to such aircraft, ATC should take into consideration the existing or anticipated traffic situation and may have to modify the time of departure, flight level or route of the intended flight. Subsequent adjustments may become necessary during the course of the flight.</li>
    </ol>
