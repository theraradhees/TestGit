    <hr>

    <ol type="a">
        <li>Upon receipt of advice that an aircraft is making an emergency descent through other traffic, all possible action shall be taken immediately to safeguard all aircraft concerned. When deemed necessary, air traffic control units shall immediately broadcast by means of the appropriate radio aids, or if not possible, request the appropriate communications stations immediately to broadcast an emergency message.</li><p></p>
        <li>It is expected that aircraft receiving such a broadcast will clear the specified areas and stand by on the appropriate radio frequency for further clearances from the air traffic control unit. Immediately after such an emergency broadcast has been made the ACC, the approach control unit, or the aerodrome control tower concerned shall forward further clearances to all aircraft involved as to additional procedures to be followed during and subsequent to the emergency descent. The ATS unit concerned shall additionally inform any other ATS units and control sectors which may be affected.</li><p></p>
        <li>ACTION BY THE ATS UNIT</li>
        Upon recognition that an aircraft is making an emergency descent, all appropriate action shall be taken immediately to safeguard all aircraft concerned. Appropriate actions may include the following, in the order appropriate for the circumstance:
        <ol>
            <li>broadcasting an emergency message;</li>
            <li>issuing traffic information and/or instructions to aircraft affected by the descent;</li>
            <li>advising the minimum flight altitude and altimeter setting for the area of operation; and</li>
            <li>informing any other ATS units which may be affected by the emergency descent</li>
        </ol>
    </ol>
