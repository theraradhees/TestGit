<h2>Strayed Visual Flight Rules (VFR) flights</h2>
<ol type="a">
    <li>Strayed aircraft</li>
    <p>
        An aircraft which has deviated significantly from its intended track or which reports that it is lost. VFR flights encountering adverse meteorological conditions
    </p>
    <li>Unidentified aircraft</li>
    <p>
        An aircraft which has been observed or reported to be operating in a given area but whose identity has not been established.
    </p>
    <li>If the aircraft’s position is not known, the air traffic services unit shall:</li>
    <ol>
        <li>attempt to establish two-way communication with the aircraft, unless such communication already exists;</li>
        <li>use all available means to determine its position;</li>
        <li>inform other ATS units into whose area the aircraft may have strayed or may stray, taking into account all the factors which may have affected the navigation of the aircraft in the circumstances;</li>
        <li>inform, in accordance with locally agreed procedures, appropriate military units and provide them with pertinent flight plan and other data concerning the strayed aircraft;</li>
        <li>request from the units referred to points above and from other aircraft in flight every assistance in establishing communication with the aircraft and determining its position.</li>
    </ol>
    <p></p>
    <li>When the aircraft’s position is established, the air traffic services unit shall:</li>
    <ol>
        <li>advise the aircraft of its position and corrective action to be taken; and</li>
        <li>provide, as necessary, other ATS units and appropriate military units with relevant information concerning the strayed aircraft and any advice given to that aircraft.</li>
    </ol>
    <p></p>
    <li>As soon as an air traffic services unit becomes aware of an unidentified aircraft in its area, it shall endeavour to establish the identity of the aircraft whenever this is necessary for the provision of air traffic services or required by the appropriate
        military authorities in accordance with locally agreed procedures. To this end, the air traffic services unit shall take such of the following steps as are appropriate in the circumstances:</li>
    <ol>
        <li>attempt to establish two-way communication with the aircraft;</li>
        <li>inquire of other air traffic services units within the FIR about the flight and request their assistance in establishing two-way communication with the aircraft;</li>
        <li>inquire of air traffic services units serving the adjacent FIRs about the flight and request their assistance in establishing two-way communication with the aircraft;</li>
        <li>attempt to obtain information from other aircraft in the area.</li>
    </ol>
    <p></p>
    <li>The air traffic services unit shall, as necessary, inform the appropriate military unit as soon as the identity of the aircraft has been established.</li>
    <p></p>
    <li>Should the ATS unit consider that a strayed or unidentified aircraft may be the subject of unlawful interference, the appropriate authority designated by the State shall immediately be informed, in accordance with locally agreed procedures.</li>
</ol>
