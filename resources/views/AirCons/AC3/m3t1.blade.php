<h2>Air traffic incident report</h2>
<ol>
    <li>An air traffic incident report shall be submitted, for incidents specifically related to the provision of air traffic services involving such occurrences as aircraft proximity (AIRPROX) or other serious difficulty resulting in a hazard to aircraft,
        caused by, among others, faulty procedures, non-compliance with procedures, or failure of ground facilities.</li>
    <p></p>
    <li>The degree of risk involved in aircraft proximity should be determined in the incident investigation and classified as “risk of collision”, “safety not assured”, “no risk of collision” or “risk not determined”.</li>
</ol>
