<h2>Weather deviation procedures</h2>
<p>
    The following procedures are intended for deviations around adverse meteorological conditions.
</p>
<ol type="a">
    <li>When the pilot initiates communications with ATC, a rapid response may be obtained by stating “WEATHER DEVIATION REQUIRED” to indicate that priority is desired on the frequency and for ATC response. When necessary, the pilot should initiate the communications
        using the urgency call “PAN PAN” (preferably spoken three times).aircraft flight conditions;</li>
    <p></p>
    <li>The pilot shall inform ATC when weather deviation is no longer required, or when a weather deviation has been completed and the aircraft has returned to its cleared route.</li>
    <p></p>
    <li>Actions to be taken when controller-pilot communications are established ATC should take one of the following actions:</li>
    <ol>
        <li>when appropriate separation can be applied, issue clearance to deviate from track; or</li>
        <li>if there is conflicting traffic and ATC is unable to establish appropriate separation, ATC shall:</li>
        <ol type="i">
            <li>advise the pilot of inability to issue clearance for the requested deviation;</li>
            <li>advise the pilot of conflicting traffic; and</li>
            <li>request the pilot’s intentions.</li>

        </ol>
    </ol>
    <li>Actions to be taken if a revised ATC clearance cannot be obtained</li>
    <p>
        If the aircraft is required to deviate from track to avoid adverse meteorological conditions and prior clearance cannot be obtained, an ATC clearance shall be obtained at the earliest possible time. Until an ATC clearance is received, the pilot shall
        take the following actions:
    </p>
    <ol>
        <li>if possible, deviate away from an organized track or route system;</li>
        <li>establish communications with and alert nearby aircraft by broadcasting, at suitable intervals: aircraft identification, flight level, position (including ATS route designator or the track code) and intentions, on the frequency in use and on 121.5
            MHz (or, as a backup, on the inter-pilot air-to-air frequency 123.45 MHz);</li>
        <li>watch for conflicting traffic both visually and by reference to ACAS (if equipped);</li>
        <li>turn on all aircraft exterior lights (commensurate with appropriate operating limitations);</li>
        <li>for deviations of less than 19 km (10 NM) remain at a level assigned by ATC;</li>
        <li>for deviations greater than 19 km (10 NM), when the aircraft is approximately 19 km (10 NM) from track, initiate a level change in accordance with Table below;</li>
        <p style="text-align: center"><strong>Table3.1 level change</strong></p>
        <p style="text-align: center"><strong><img src="https://www.trainairplus.id/draftfile.php/5/user/draft/796716546/Table%203.1%20level%20change%20%281%29.png" alt="" role="presentation"><br></strong></p>
        <li>when returning to track, be at its assigned flight level when the aircraft is within approximately 19 km (10 NM) of the centre line; and</li>
        <li>if contact was not established prior to deviating, continue to attempt to contact ATC to obtain a clearance. If contact was established, continue to keep ATC advised of intentions and obtain essential traffic information.</li>
