<h2>Procedures in regard to aircraft equipped with Airborne Collision Avoidance Systems (ACAS)</h2>
<ol type="a">
    <li>The procedures to be applied for the provision of air traffic services to aircraft equipped with ACAS shall be identical to those applicable to non-ACAS equipped aircraft. In particular, the prevention of collisions, the establishment of appropriate
        separation and the information which might be provided in relation to conflicting traffic and to possible avoiding action shall conform with the normal ATS procedures and shall exclude consideration of aircraft capabilities dependent on ACAS equipment.</li>
    <p></p>
    <li>When a pilot reports an ACAS resolution advisory (RA), the controller shall not attempt to modify the aircraft flight path until the pilot reports “Clear of Conflict”.</li>
    <p></p>
    <li>Once an aircraft departs from its ATC clearance or instruction in compliance with an RA, or a pilot reports an RA, the controller ceases to be responsible for providing separation between that aircraft and any other aircraft affected as a direct consequence
        of the manoeuvre induced by the RA. The controller shall resume responsibility for providing separation for all the affected aircraft when:</li>
    <ol>
        <li>the controller acknowledges a report from the flight crew that the aircraft has resumed the current clearance; or</li>
        <li>the controller acknowledges a report from the flight crew that the aircraft is resuming the current clearance and issues an alternative clearance which is acknowledged by the flight crew.</li>
    </ol>
    <p></p>
    <li>Following a significant ACAS event, pilots and controllers should complete an air traffic incident report.</li>
</ol>
