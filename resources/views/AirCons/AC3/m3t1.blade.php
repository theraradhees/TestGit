<h2>Interception of civil aircraft</h2>
<ol type="a">
    <li>As soon as an air traffic services unit learns that an aircraft is being intercepted in its area of responsibility, it shall take such of the following steps as are appropriate in the circumstances:</li>
    <ol>
        <li>attempt to establish two-way communication with the intercepted aircraft via any means available, including the emergency frequency 121.5 MHz, unless such communication already exists;</li>
        <li>inform the pilot of the intercepted aircraft of the interception;</li>
        <li>establish contact with the intercept control unit maintaining two-way communication with the intercepting aircraft and provide it with available information concerning the aircraft;</li>
        <li>relay messages between the intercepting aircraft or the intercept control unit and the intercepted aircraft, as necessary;</li>
        <li>in close coordination with the intercept control unit take all necessary steps to ensure the safety of the intercepted aircraft; and</li>
        <li>inform ATS units serving adjacent FIRs if it appears that the aircraft has strayed from such adjacent FIRs.</li>
    </ol>
    <p></p>
    <li>As soon as an air traffic services unit learns that an aircraft is being intercepted outside its area of responsibility, it shall take such of the following steps as are appropriate in the circumstances:</li>
    <ol>
        <li>Inform the ATS unit serving the airspace in which the interception is taking place, providing this unit with available information that will assist in identifying the aircraft and requesting it to take action in accordance with 1.3.1</li>
        <li>Relay messages between the intercepted aircraft and the appropriate ATS unit, the intercept control unit or the intercepting aircraft.</li>
    </ol>
</ol>
