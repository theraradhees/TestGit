<h2>Fuel Dumping</h2>
<ol>
    <li>An aircraft in an emergency or other urgent situations may need to dump fuel so as to reduce to maximum landing mass in order to effect a safe landing.</li>
    <p></p>
    <li>When an aircraft operating within controlled airspace needs to dump fuel, the flight crew shall advise ATC. The ATC unit should then coordinate with the flight crew the following:</li>
    <ol>
        <li>the route to be flown, which, if possible, should be clear of cities and towns, preferably over water and away from areas where thunderstorms have been reported or are expected;</li>
        <li>the level to be used, which should be not less than 1800 m (6000 ft); and</li>
        <li>the duration of the fuel dumping.</li>
    </ol>
    <p></p>
    <li>Other known traffic should be separated from the aircraft dumping fuel by:</li>
    <ol>
        <li>at least 19 km (10 NM) horizontally, but not behind the aircraft dumping fuel;</li>
        <li>vertical separation if behind the aircraft dumping fuel within 15 minutes flying time or a distance of 93 km (50 NM) by:</li>
        <ol type="i">
            <li>at least 300 m (1 000 ft) if above the aircraft dumping fuel; and</li>
            <li>at least 900 m (3 000 ft) if below the aircraft dumping fuel.</li>
            <p>
                <i>The horizontal boundaries of the area within which other traffic requires appropriate vertical separation extend for 19 km (10 NM) either side of the track flown by the aircraft which is dumping fuel, from 19 km (10 NM) ahead, to 93 km (50 NM) or 15 minutes along track behind it (including turns).</i>
            </p>
        </ol>
    </ol>
    <li>A warning message shall be broadcast on appropriate frequencies for non-controlled traffic to remain clear of the area concerned. Adjacent ATC units and control sectors should be informed of the fuel dumping taking place and requested to broadcast
        on applicable frequencies an appropriate warning message for other traffic to remain clear of the area concerned. </li>
    <p>Upon completion of the fuel dumping, adjacent ATC units and control sectors should be advised that normal operations can be resumed.</p>
</ol>
