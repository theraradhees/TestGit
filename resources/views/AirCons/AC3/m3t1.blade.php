<h2>Change of radiotelephony call sign for aircraft</h2>
<ol type="a">
    <li>An ATC unit may instruct an aircraft to change its type of RTF call sign, in the interests of safety, when similarity between two or more aircraft RTF call signs are such that confusion is likely to occur.</li>
    <p></p>
    <li>Any such change to the type of call sign shall be temporary and shall be applicable only within the airspace(s) where the confusion is likely to occur.</li>
</ol>
