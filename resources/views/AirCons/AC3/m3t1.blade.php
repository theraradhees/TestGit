<h2>Unauthorized use of ATC frequency</h2>
<ol type="a">
    <li>Instances of false and deceptive transmissions on ATC frequencies which may impair the safety of aircraft can occasionally occur. In the event of such occurrences, the ATC unit concerned should:</li>
    <ol>
        <li>correct any false or deceptive instructions or clearances which have been transmitted;</li>
        <li>advise all aircraft on the affected frequency(ies) that false and deceptive instructions or clearances are being transmitted;</li>
        <li>instruct all aircraft on the affected frequency(ies) to verify instructions and clearances before taking action to comply;</li>
        <li>if practical, instruct aircraft to change to another frequency; and</li>
        <li>if possible, advise all aircraft affected when the false and deceptive instructions or clearances are no longer being transmitted.</li>
    </ol>
    <p></p>
    <li>Flight crews shall challenge or verify with the ATC unit concerned any instruction or clearance issued to them which they suspect may be false or deceptive.</li>
    <p></p>
    <li>When the transmission of false or deceptive instructions and clearances is detected, the appropriate authority shall take all necessary action to have the transmitter located and the transmission terminated.</li>
</ol>
<br>
<h2>Blocked frequency</h2>
<p>In the event that the control frequency is inadvertently blocked by an aircraft transmitter, the following additional steps should be taken:</p>
<ol type="a">
    <li>attempt to identify the aircraft concerned;</li>
    <p></p>
    <li>if the aircraft blocking the frequency is identified, attempts should be made to establish communication with that aircraft, e.g. on the emergency frequency 121.5 MHz, by SELCAL, through the aircraft operator’s company frequency if applicable, on
        any VHF frequency designated for air-to-air use by flight crews or any other communication means or, if the aircraft is on the ground, by direct contact;</li>
    <p></p>
    <li>if communication is established with the aircraft concerned, the flight crew shall be instructed to take immediate action to stop inadvertent transmissions on the affected control frequency.</li>
</ol>
