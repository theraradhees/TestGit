    <h1>Emergency Procedures</h1>
    <hr>
    <p>
        When an emergency is declared by an aircraft, the ATS unit should take appropriate and relevant action as follows:
    </p>
    <ol type="a">
        <li>unless clearly stated by the flight crew or otherwise known, take all necessary steps to ascertain aircraft identification and type, the type of emergency, the intentions of the flight crew as well as the position and level of the aircraft;</li>
        <p></p>
        <li>decide upon the most appropriate type of assistance which can be rendered;</li>
        <p></p>
        <li>enlist the aid of any other ATS unit or other services which may be able to provide assistance to the aircraft;</li>
        <p></p>
        <li>provide the flight crew with any information requested as well as any additional relevant information, such as details on suitable aerodromes, minimum safe altitudes, weather information;</li>
        <p></p>
        <li>obtain from the operator or the flight crew such of the following information as may be relevant: number of persons on board, amount of fuel remaining, possible presence of hazardous materials and the nature thereof; and</li>
        <p></p>
        <li>notify the appropriate ATS units and authorities as specified in local instructions.</li>
        <p></p>
        <li>Changes of radio frequency and SSR code should be avoided if possible and should normally be made only when or if an improved service can be provided to the aircraft concerned. Manoeuvreing instructions to an aircraft experiencing engine failure should be limited to a minimum. When appropriate, other aircraft operating in the vicinity of the aircraft in emergency should be advised of the circumstances.</li>
        <p></p>
        <li>An aircraft known or believed to be in a state of emergency, including being subjected to unlawful interference, shall be given priority over other aircraft.</li>
        <p></p>
    </ol>
