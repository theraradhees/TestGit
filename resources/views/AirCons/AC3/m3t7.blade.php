<div>
    <h1>ATS surveillance system failure</h1>
    <hr>
    <ol type="a">
<br>
        <li>In the event of complete failure of the ATS surveillance system where air-ground communications remain, the controller shall plot the positions of all aircraft already identified, take the necessary action to establish procedural separation between the aircraft and, if necessary, limit the number of aircraft permitted to enter the area.</li><p></p>
        <li>As an emergency measure, use of flight levels spaced by half the applicable vertical separation minimum may be resorted to temporarily if standard procedural separation cannot be provided immediately.</li>

    </ol>
</div>
