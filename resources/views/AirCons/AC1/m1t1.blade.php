    <h1>Surveillance System</h1>
    <hr>
    <h2>Primary Surveillance Radar (PSR)</h2>
    <p>
        Where PSR is used for identification, aircraft may be identified by one or more of the following procedures: 
    </p>
    <ol type="a">
        <li>by correlating a particular radar position indication with an aircraft reporting its position over, or as bearing and distance from, a point shown on the situation display, and by ascertaining that the track of the particular radar position is consistent with the aircraft path or reported heading; </li>
        <li>by correlating an observed radar position indication with an aircraft which is known to have just departed, provided that the identification is established within 2 km (1 NM) from the end of the runway used. Particular care should be taken to avoid confusion with aircraft holding over or overflying the aerodrome, or with aircraft departing from or making a missed approach over adjacent runways; </li>
        <li>by transfer of identification </li>
        <li>by ascertaining the aircraft heading, if circumstances require, and following a period of track observation: </li>
        <ol>
            <li>instructing the pilot to execute one or more changes of heading of 30 degrees or more and correlating the movements of one particular radar position indication with the aircraftâ€™s acknowledged execution of the instructions given; or </li>
            <li>correlating the movements of a particular radar position indication with manoeuvre currently executed by an aircraft having so reported. </li>
        </ol>
        <li>When using these methods, the controller shall: </li>
        <ol>
            <li>verify that the movements of not more than one radar position indication correspond with those of the aircraft; and </li>
            <li>ensure that the manoeuvre(s) will not carry the aircraft outside the coverage of the radar or the situation display. </li>
        </ol>
    </ol>
    <br>
    <h2>Secondary Surveillance Radar (SSR) and/or Multilateration (MLAT) </h2>
    <p>
        Where SSR and/or MLAT is used for identification, aircraft may be identified by one or more of the following procedures: 
    </p>
    <ol type="a">
        <li>recognition of the aircraft identification in an SSR and/or MLAT label; </li>
        <li>recognition of an assigned discrete code, the setting of which has been verified, in an SSR and/or MLAT label; and </li>
        <li>direct recognition of the aircraft identification of a Mode S-equipped aircraft in an SSR and/or MLAT label; </li>
        <li>by transfer of identification </li>
        <li>observation of compliance with an instruction to set a specific code; </li>
        <li>observation of compliance with an instruction to squawk IDENT. </li>
    </ol>
    <br>
    <h2>Additional Identification Method</h2>
    <p>
        When two or more position indications are observed in close proximity, or are observed to be making similar movements at the same time, or when doubt exists as to the identity of a position indication for any other reason, changes of heading should be prescribed or repeated as many times as necessary, or additional methods of identification should be employed, until all risk of error in identification is eliminated. 
    </p>
