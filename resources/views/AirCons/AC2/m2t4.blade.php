    <h1 style="text-align: center">Short-Term Conflict Alert (STCA) </h1>
    <hr>
    <ol type="a">
        <li>In the event an STCA is generated in respect of controlled flights, the controller shall without delay assess the situation and, if necessary, take action to ensure that the applicable separation minimum will not be infringed or will be restored.</li><p></p>
        <li>
            Following the generation of an STCA, controllers should be required to complete an air traffic incident report only in the event that a separation minimum was infringed.
        </li>
        <p></p>
        <li>The appropriate ATS authority should retain electronic records of all alerts generated. The data and circumstances pertaining to each alert should be analysed to determine whether an alert was justified or not. Non-justified alerts, e.g. when visual separation was applied, should be ignored. A statistical analysis should be made of justified alerts in order to identify possible shortcomings in airspace design and ATC procedures as well as to monitor overall safety levels.</li>
    </ol>
