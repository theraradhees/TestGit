    <h1 style="text-align: center">Transfer of control</h1>
    <hr>
    <ol type="a">
        <li>Where an ATS surveillance service is being provided, transfer of control should be effected, whenever practicable, so as to enable the uninterrupted provision of the ATS surveillance service.</li>
        <p></p>
        <li>Where SSR and/or ADS-B and/or MLAT is used and the display of position indications with associated labels is provided for, transfer of control of aircraft between adjacent control positions or between adjacent ATC units may be effected without prior coordination, provided that:</li>
        <ol>
            <li>updated flight plan information on the aircraft about to be transferred, including the discrete assigned SSR code or, with respect to Mode S and ADS-B, the aircraft identification, is provided to the accepting controller prior to transfer;</li>
            <li>the ATS surveillance system coverage provided to the accepting controller is such that the aircraft concerned is presented on the situation display before the transfer is effected and is identified on, but preferably before, receipt of the initial call;</li>
            <li>when the controllers are not physically adjacent, two-way direct speech facilities, which permit communications to be established instantaneously, are available between them at all times;</li>
            <li>the transfer point or points and all other conditions of application, such as direction of flight, specified levels, transfer of communication points, and especially an agreed minimum separation between aircraft, including that applicable to succeeding aircraft on the same route, about to be transferred as observed on the situation display, have been made the subject of specific instructions (for intra-unit transfer) or of a specific letter of agreement between two adjacent ATC units;</li>
            <li>the instructions or letter of agreement specify explicitly that the application of this type of transfer of control may be terminated at any time by the accepting controller, normally with an agreed advance notice;</li>
            <li>the accepting controller is informed of any level, speed or vectoring instructions given to the aircraft prior to its transfer and which modify its anticipated flight progress at the point of transfer.</li>
        </ol>
        <p></p>
        <li>Where primary radar is being used, and where another type of ATS surveillance system is employed but the provisions of 8.7.4.2 are not applied, the transfer of control of aircraft between adjacent control positions or between two adjacent ATS units may be effected, provided that:</li>
        <ol>
            <li>identification has been transferred to or has been established directly by the accepting controller;</li>
            <li>when the controllers are not physically adjacent, two-way direct-speech facilities between them are at all times available which permit communications to be established instantaneously;</li>
            <li>separation from other controlled flights conforms to the minima authorized for use during transfer of control between the sectors or units concerned;</li>
            <li>
                the accepting controller is informed of any level, speed or vectoring instructions applicable to the aircraft at the point of transfer;
            </li>
            <li>radio communication with the aircraft is retained by the transferring controller until the accepting controller has agreed to assume responsibility for providing the ATS surveillance service to the aircraft. Thereafter, the aircraft should be instructed to change over to the appropriate channel and from that point is the responsibility of the accepting controller.</li>
            <p></p>
        </ol>

    </ol>
