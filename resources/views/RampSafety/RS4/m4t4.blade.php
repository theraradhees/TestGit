<div style="text-align: justify"
  <p>
    An accident or safety occurrence can indicate that there is a failure with in the safety management system. It is therefore important that all accidents and safety occurrences are reported and investigated. 
  </p>
  <p>
    Commonly, The Airfield Operations Department is the central reporting point for the collection and collation of all information on the aprons / ramps at the airport.
  </p>
  <p>
    An accident and safety occurrence investigation and reporting system is in place at Airport to ensure that action is taken to safe guard life and property, including aircraft, vehicles, equipment and airport infrastructure.
  </p>
  <p>
    Actions required under the term so this procedure do not replace any actions that maybe required by individual employers or absolve any person from their responsibilities for any reporting procedure required by Statute or Regulation 
  </p>
  <p>
    Accidents and safety occurrences must be reported without delay to Operations Control on any special <u>hotline</u> numbers giving details of: 
  </p>
  <ul >
    <li>The name and company of the caller</li>
    <li>Location of accident / safety occurrence </li>
    <li>Injuries to personnel </li>
    <li>Injuries to personnel </li>
    <li>A description of what happened </li>
    <li>What immediate assistance is required </li>
  </ul>
  <p>
    The scene of the accident or safety occurrence should be isolated and the vehicles, plan and equipment involved in the accident or safety occurrence should not normally be moved until the Airfield Operations Supervisor is in attendance. However, if in the judgment of the senior person present or the Officer in Charge of the Fire Service their removal is necessary in the interests of safety or to affect a rescue, this may be done. The scene should prefer ably be photographed before being disturbed, by digital camera if possible. 
  </p>
  <p>
    Reports of safety occurrences or unsafe conditions / practices can be made anonymously to the special <u>hotline</u> numbers, or in writing to the Airfield Operations Supervisor
  </p>


</div>