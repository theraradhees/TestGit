  <h2>Spillage</h2>
  <p>
    Fuel, oil, solvents, paint, adhesives, detergents, herbicides and pesticides, amongst other things, are all classified as Dangerous Goods or Hazardous Substances and are potentially dangerous or harmful to human health and the environment. 
  </p>
  <p>
    Poor storage, handling and disposal of these products not only create a risk to human health, it can also cause environmental (soil, air and water) pollution. The cost to clean up environmental pollution can be extremely expensive. Prevention of spills is the first step in avoiding risk to human health and the environment. An effective response and good management of the spills that do occur is vital if risks to human health and environmental pollution are to be avoided or minimized.
  </p>
  <h3>Spill Response Equipment </h3>
  <p>
    All operators on airport are required to maintain sufficient response equipment to manage the type and size of spill that may occur at their premises, or in association with their work. There is a variety of spill response equipment. The type and quantity of fuel, oil and chemicals you use and store at your facility will determine the type and quantity of spill response equipment you require. Spill response material designed to target specific substances is commercially available. For example absorbent mats and booms designed to absorb fuel, oil and allow water to pass through is available. 
  </p>
  <p>Spill Response Equipment can include: </p>
  <ul>
    <li>Absorbent Booms and Mats </li>
    <li> Granules </li>
    <li> Storm water Drain Caps </li>
    <li>Portable Bunds </li>
  </ul>
  <p>
    In facilities where small amounts (<20L) of Dangerous Goods and Chemicals are used and stored, absorbent substances such as chemisorb or saw dust are sufficient for spill response. 
  </p>
  <br>
  <h2>Procedure in Event Of Spillage </h2>
  <p>

  </p>
  This procedure applies to all spillage including aviation fuel, hydraulic oil, and chemical. The general procedure are: 
  </div>
  <ul>
    <li>Fuel spillage covering an area of approximately 4 m2 or more constitutes a hazard and should be brought to the immediate attention of the Airport Emergency Service; </li>
    <li>Do not drive through the spill liquid. Report spill to Apron Control Centre and Airport Emergency Service;</li>
    <li>Do not start engine of motorized equipment within spill areas until it is cleaned up (fuel spill in case).; </li>
    <li>Cordon off the spillage area; </li>
    <li>Divert road traffic; </li>
    <li>No vehicle/ground equipment shall be permitted to come within 15 meters of the spillage area (fuel spill in case). </li>
    <li>Clean up the spillage if only it is safe to do so, apply absorbent material to absorb the spill, sweep up the residue and place in container for disposal. </li>
  </ul>
