  <h3>Call For Help Before Attempting To Extinguish A Serious Fire.</h3>
  <p>
    The fire may take hold much faster than you're capable of dealing with it, and if help is on the way, it removes one less concern for you.
  </p>
  <ul>
    <li>Call, or have someone else call, 911 in North America, 999 in the United Kingdom, 112 in the European Union and many other countries (or the appropriate emergency number for your country) as soon as possible. Ask for the fire service to come immediately, giving your address and a brief description of the type of fire. </li>
    <li>Check that all other people are out of the house and have them remove pets as well. Check that they're all assembled at a safe meeting point. Do not allow children to attempt to use a fire extinguisher or control a fire in any respect whatsoever. </li>
    <li>Realize that reacting to a fire requires a sound process of decision-making that children and some adults may not be capable of coping with their panic. Prior training on correct use can alleviate some of the concern here.</li>
  </ul>
  <h3>Check For Your Own Safety Before Starting To Extinguish A Fire. </h3>
  <p>There are some key things to check for before you start fighting a fire using a fire extinguisher: </p>
  <ul>
    <li>Are you physically capable of extinguishing a fire? Some people have physical limitations that might diminish or eliminate their ability to properly use a fire extinguisher. People with disabilities, older adults, or children may find that an extinguisher is too heavy to handle or it may be too difficult for them to exert the necessary pressure to operate the extinguisher. </li>
    <li>Look for your exit points. Ensure that there is a clear exit for immediate escape should this becomes necessary. At all times, keep your mind focused on the availability of a safe retreat. If this is threatened at all, leave at once. The US National Fire Prevention Association recommends that you install fire extinguishers close to an exit point, to enable you to keep your back to the exit when you use the extinguisher; this ensures that you can make an easy escape if the fire cannot be controlled. </li>
    <li>Do not attempt to put out a fire where it is emitting toxic smoke; if you suspect or simply don't know if the smoke is toxic, leave it to the professionals </li>
    <li>Check for structural safety of the building, in case burning walls, floors, or rafters pose a risk to your safety. </li>
    <li>If you have more than one fire extinguisher, consider asking another mature and responsible person to use it in tandem with you. </li>
    <li>Remember that your life is more important than property, so don't place yourself or others at risk. </li>
  </ul>
