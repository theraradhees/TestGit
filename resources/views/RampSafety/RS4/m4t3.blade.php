  <h3>Assess the Fire.</h3>
  <p>
    Only a contained fire should be fought using a fire extinguisher. Portable fire extinguishers are valuable for immediate use on small fires because they contain a limited amount of extinguishing material, which needs to be used properly or it will be wasted. For example, when a pan initially catches fire, it may be safe to turn off the burner, place a lid on the pan, and use an extinguisher. By the time the fire has spread, however, these actions will not be adequate, and only trained firefighters can safely extinguish such fires. 
  </p>
  <ul>
    <li>Make a quick commonsense assessment about the utility and safety of using a fire extinguisher for the fire you're experiencing. Obviously, a fire extinguisher is overkill for a candle but it's useless when the whole house is on fire. A fire in a wastepaper basket, however, is another suitable candidate for fire extinguisher use. </li>
    <li>Use your instincts. If your instincts tell you the fire's too dangerous to tackle, trust them. </li>
    <p></p>
  </ul>
  <h3>Check the Type of Extinguisher</h3>
  <p>
  In the USA, there are five main classes of fire extinguisher (note that the classes and nomenclature can vary from country to country): A, B, C, and the less common classes D and K. The extinguishing agent might be water, dry chemical, halon, CO2, or a special powder. 
  </p>
  <ul>
    <li><b> Class A: </b> This is suitable for cloth, wood, rubber, paper, various plastics, and regular combustible fires. It is usually filled with 2 1/2 gallons (9.46 liters) of pressurized water. </li>
    <li><b>Class B: </b> This is suitable for grease, gasoline or oil-based fires is usually filled with a dry chemical. Extinguishers smaller than 6lbs (2.72kg) are not recommended. </li>
    <li><b> Class C: </b> This is suitable for electrical fires caused by appliances, tools, and other plugged in gear. It can contain either halon or CO2. Halon 1211 and 1301 is very expensive and depletes the ozone layer, but it is being replaced by non-depleting agents such as FM200. Note that halon is now illegal in numerous jurisdictions. </li>
    <li><b>Class D: </b> This is used for water-reactive metals such as burning magnesium and will be located in factories using such metals. It comes in the form of a powder that must cover the material to extinguish it.</li>
    <li><b>Class K: </b> This contains a special purpose wet chemical agent for use in kitchen fires and deep fryers to stop fires started by vegetable oils, animal fats, or other fats started in cooking appliances.</li>
  </ul>
  <p>
  Note that many fire extinguishers will work on a combination of fire classes. You'll need to decide quickly on what type of fire you have and ensure that your fire extinguisher is compatible with the fire you are attempting to extinguish. An all-purpose ABC dry chemical (10lb/4.5kg) extinguisher is a safe bet for most fires, especially where you're not sure of the fire's origins. 
  </p>
  <h3>Ready The Fire Extinguisher. </h3>
  <p>
  Almost all fire extinguishers have a safety pin in the handle. This pin usually looks like a plastic or metal ring, sometimes colored red that is held in place by a plastic seal. The distinctive features will vary depending on the type of fire extinguisher you have. You must break the seal and pull the safety pin from the handle before you can use the fire extinguisher by squeezing the lever, which discharges the fire extinguishing agent. 
  </p>
  <p>
    It helps if you ensure that you're familiar with how your fire extinguisher works before being placed under pressure; take time to read over its instructions after you've read this article. Familiarize yourself with its special features and parts. Different extinguishers rely on different methods of use: be aware of this in advance of having to use them. 
  </p>
  <h3>Aim for the Base of The Fire.</h3>
  <p>
  Shooting into the flame is a waste of the fire extinguisher, as you're not putting out the source of the flames. It's vital to stop the fire at its source, or to remove or dampen the fuel from the fire, in order to put it out. By focusing the extinguisher's spray at the base of the fire or the source, you're extinguishing the fuel.
  </p>
  <h3>Remember The Simple Acronym P.A.S.S. </h3>
  <p>
    P.A.S.S to help you use the fire extinguisher effectively.P.A.S.S. stands for: Pull, Aim, Squeeze, Sweep, explained below the printable diagram. 
  </p>
  <ul>
    <li>Pull the safety pin from the handle. The pin is located at the top of the fire extinguisher. Once removed, it releases the locking mechanism, allowing you to discharge the extinguisher.</li>
    <li>Aim the extinguisher nozzle or hose at the base of the fire. As explained, this removes the source or fuel of the fire. Keep yourself low. </li>
    <li>Squeeze the handle or lever slowly to discharge the agent. Letting go of the handle will stop the discharge, so keep it held down. </li>
    <li>Sweep side to side approximately 6in or 15cm over the fire until expended. The sweeping motion helps to extinguish the fire. Stand several feet or meters back from the fire: fire extinguishers are manufactured for use from a distance. </li>
  </ul>
  <p>
    The fire may flare up somewhat as extinguishing begins due to the flames being pushed away from the burning material (the real target) by the agent and gust of propellant. Do not be alarmed so long as it dies back promptly. 
  </p>
  <h3>Be aware that the typical fire extinguisher will contain around 10 seconds of extinguishing power. </h3>
  <ul>
    <li>If the extinguisher has already been discharged partially, this time will be less. </li>
    <li>If the fire doesn't respond well after you've used up the fire extinguisher, remove yourself to safety quickly. </li>
    <li>If the room fills with smoke, make a hasty exit</li>
  </ul>
  <h3>Tend to the area if you have successfully put out the fire.</h3>
  <p>
    This means not leaving it alone, as it might re-ignite without warning. If it is safe to do so, remove fuel sources and commence cleaning up. Water can be used to ensure there are no remaining sparks in materials that are safe to extinguish with water (most combustibles other than oils and other insoluble combustible liquids or places involving electricity). The fire department could help you ensure the fire is completely extinguished. If you're legally obliged to report fires to local authorities, then do so, especially if filing for an insurance claim. 
  </p>
