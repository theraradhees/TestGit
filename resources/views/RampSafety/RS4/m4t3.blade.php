  <h2>Foreign Object Debris (FOD)</h2>
  <p>
    Foreign Object Debris (FOD), FOD is any object, live or not, located in an inappropriate location in the airport environment that has the capacity to injure airport or air carrier personnel and damage aircraft. Facilities and ramp area must be kept clean if we are to avoid FOD caused personal injury, which can result in lost wage or even permanent disability or aircraft damage that can cause flights to be late or canceled. 
  </p>
  <p>
    FOD includes a wide range of material, including loose hardware, pavement fragments, catering supplies, building materials, rocks, sand, pieces of luggage, and even wildlife. FOD is found at terminal gates, cargo aprons, taxiways, runways, and run-up pads. It causes damage through direct contact with airplanes, such as by cutting airplane tires or being ingested into engines, or as a result of being thrown by jet blast and damaging airplanes or injuring people. 
  </p>
  <h3>FOD Prevention </h3>
  <p>
    FOD Prevention is your responsibility. Effective Foreign Object Debris (FOD) prevention requires your daily attention to these very important procedure: 
  </p>
  <ul>
    <li>Checking the ramp and gate areas for cleanliness and deterioration. </li>
    <li>Checking ground equipment and parking area for cleanliness</li>
    <li>Ensure there are no loose parts on ground equipment that could fall. </li>
    <li>Ensure that working area are clear of Foreign Object Debris (FOD).</li>
  </ul>
