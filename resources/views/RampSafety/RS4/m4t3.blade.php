  <h2>Light Gun Signals</h2>
  <p><i>[www.faa.gov/go/runwaysafety] </i></p>
  <p>
    Air traffic controllers have a backup system for communicating if radios fail. Controllers use a light gun that flashes different colors to tell pilots or vehicle drivers what to do. Even a failed radio is not an excuse for proceeding without a proper clearance. If you are on a runway or taxiway and radio communication fails, you should:
  </p>
  <ul>
    <li>Turn toward the tower. </li>
    <li>Flash your lights several times. </li>
    <li>Wait for the controller to signal you with the light gun. </li>
    <li>Be patient for a response. If the controllerâ€™s attention is diverted, it may take a few minutes for a response. If your radios are working, try a frequency other than ground. </li>
    <li>Call the tower on your cell phone if you have the number available. </li>
