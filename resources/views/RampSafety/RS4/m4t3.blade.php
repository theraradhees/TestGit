  <h1>Abnormal Situation (Aerodrome Emergency) Responses </h1>
  <hr>

  <p>
    An aerodrome emergency is an actual or imminent occurrence, which may endanger the safety or health of any person or may destroy or damage property. There are many possible emergency situations at an airport however those emergencies specifically involving aircraft are defined by two categories, which determine the level of response required. These categories are: 
  </p>
  <ul>
    <li>Full emergency: </li>
    <p>
      A situation declared by either the Pilot in Command or Air Traffic Control, when it is known that an aircraft approaching the airport is, or is suspected to be, in such trouble that there is danger of an accident requiring the response from off-airport agencies. An Airport Emergency Plan activation is required for response to this situation and will involved participation from all agencies. 
    </p>
    <li>Local Standby:</li>
    <p>
      A situation declared by either the Pilot or Command or Air Traffic Control to be initiated when an aircraft approaching the airport is known or is suspected to have developed some defect but the trouble is not such as would normally involve any serious difficulty in effecting a safe landing. Airport Emergency Plan activation is required for respond to this situation however it usually only involves airport-based agencies. 
    </p>
  </ul>
  <p>
    Airport emergencies include the following events: 
  </p>
  <ul>
    <li>Personal injuries </li>
    <li>Damage to aircraft </li>
    <li>Vehicle accidents </li>
    <li>Damage to airport infrastructure</li>
    <li>Fuel, oil or hazardous material spills</li>
  </ul>
