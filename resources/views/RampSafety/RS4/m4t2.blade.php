<div style="text-align: justify">
  <h2>General</h2>
  <p>The International Civil Aviation Organization (ICAO) defines dangerous goods as: </p>
  <p>
      “Articles or substances which are capable of posing a risk to health, safety, property or the environment and which are shown in the list of dangerous goods in the Technical Instructions or which are classified according to those Instructions.”
  </p>
  <p>
      (ICAO <i>Technical Instructions for the Safe Transport of Dangerous Goods by Air)</i>
  </p>
  <p></p>
  <p>
      Many items and substances can be dangerous when transported by air. Due to the variations in temperature and pressure during a flight, some of these items may leak or break, generating toxic fumes or possibly starting a fire. Employees who may come into
      contact with dangerous goods need to be aware of the nature of such goods, their potential for causing incidents and accidents and how they should be dealt with.
  </p>
  <p>
      The authorities allow dangerous goods to be transported by air transport, because there is a commercial demand for many products classified as dangerous goods, such as paints, batteries, goods that must be kept refrigerated for a considerable time and
      etc. Sometimes air transport is only way to reach remote places.
  </p>
  <p>
      More importantly, if the risk can be identified then it may be eliminated as a result the safely air transport can be achieved. The risk can be reduced by full compliance with the regulations. Each dangerous goods must be identified by a proper shipping
      name (PSN) that will be found in the International Air Transport Association (IATA) Dangerous Goods Regulations (DGR).
  </p>
  <br>
  <h2>Dangerous Goods Classification </h2>
  <p>
      Not all dangerous goods have the same risk, the regulation divide dangerous goods into 9 classes reflecting the type of risk involved. In some cases these classes are further sub divided into division to identify a particular risk, within that class.
  </p>
  <p>
      Classes are expressed by single digit number, for example class 4. Divisions are expressed by two digit number, for example flammable solids is class 4, division 1, and is referred to as “Division 4.1”.
  </p>
  <p>
      Each class or division has specific criteria that are used to determine whether an article or substance belongs to that class or division. These classes are:
  </p>
  <ul>
      <li>Class 1 : Explosives; </li>
      <li>Class 2 : Gases;</li>
      <li>Class 3 : Flammable Liquids; </li>
      <li>Class 4 : Flammable Solids; substances liable to spontaneous Combustion; substances which in contact with water emit flammable gases; </li>
      <li>Class 5 : Oxidizing substances and organic peroxides; </li>
      <li>Class 6 : Toxic and infectious substances;</li>
      <li>Class 7 : Radioactive material;</li>
      <li>Class 8 : Corrosives; </li>
      <li>Class 9 : Miscellaneous dangerous goods. </li>
  </ul>
