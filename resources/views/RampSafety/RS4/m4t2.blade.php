  <h2>Classes Description</h2>
  <ul>
      <li>Class 1 - Explosives; </li>
      <p></p>
      <li>Class 2.1 Flammable gases</li>
      <p>
          Flammable gases can be ignited in air. They may be lighter or heavier than air. Heavier than air gases can collect in low lying areas such as pits, depressions, and drains causing a fire and explosion hazard. Examples include: liquefied petroleum gas
          (LPG), liquefied natural gas (LNG), hydrogen, and acetylene.
      </p>
      <li>Class 2.2 - Non-flammable, non-toxic gases:</li>
      <p>
          are neither flammable nor toxic. Examples include: nitrogen, carbon dioxide, compressed air, and helium.
      </p>
      <li>Class 2.2 - Non-flammable, non-toxic gases sub-risk 5.1 (Oxidizing gases):</li>
      <p>
          These gases, although not flammable, can accelerate combustion and increase the risk of fire in the presence of combustible or flammable materials. Examples include: nitrous oxide, entonox, and nitrous oxide.
      </p>
      <li>Class 2.3 - Toxic gases: </li>
      <p>
          on inhalation these may cause death or injury. Many of these also have other properties (e.g. may be flammable, oxidizing or corrosive). Class 2.3 is never used as a subsidiary risk. If a material meets the criteria it will be classified as a toxic gas.
          Examples of toxic gases include: anhydrous ammonia, methyl bromide, sulphur dioxide, and carbon monoxide.
      </p>
      <li>Class 3 - Flammable Liquids : </li>
      <p>
          produce vapor that can be ignited in air on contact with a suitable ignition source. Such as: unleaded petrol (ULP), xylene, kerosene, and acetone.
      </p>
      <li>Class 4.1 - Flammable Solids</li>
      <p>
          these materials are easily ignited and may cause fire through friction heat or other ignition sources. Examples include: red phosphorous, hexamine, naphthalene, and camphor.
      </p>
      <li>Class 4.2</li>
      <p>
          Substances that in contact with water emit flammable gases: On contact with water these materials can become spontaneously combustible or liberate flammable or toxic gases. Examples include: aluminum phosphide (liberates phosphine gas), calcium carbide
          (liberates acetylene on contact with water), and sodium.
      </p>
      <li>Class 5.1 - Oxidizing agents: </li>
      <p>
          these substances can contribute/accelerate the combustion of other combustible or flammable materials. Examples include: ammonium nitrate, hydrogen peroxide, potassium permanganate, calcium hypochlorite.
      </p>
      <li>Class 6.1 - Toxic substances: </li>
      <p>
          These materials comprise substances liable to cause death or serious injury or harm from inhalation, ingestion or absorption through the skin. Some toxic materials may have other sub-risks (e.g. flammable or corrosive). Examples include: paraquat, endosulfan,
          arsenic compounds, cyanide, toluene di-isocyanate (TDI), and many pesticides.
      </p>
      <li>Class 7 - Radioactive Substances</li>
      <p>
          Radioactive substances comprise substances or a combination of substances which emit ionizing radiation <u>(uranium, plutonium).</u>
      </p>
      <li>Class 8 - Corrosive substances:</li>
      <p>
          corrosive materials are capable of causing the degradation and destruction of living tissue, steel and other materials on contact. Some may give off irritating vapors affecting the eyes, airways and skin. Corrosive material may possess other properties
          (e.g. flammability or oxidation). Corrosive materials are either acids or bases/alkalis. Examples of acids include: nitric acid, sulphuric acid, hydrochloric acid, lead acid batteries.
      </p>
      <li>Class 9 - Miscellaneous dangerous goods:</li>
      <p>
          This class comprises substances and articles that present a danger not offered by other classes. Some of the materials may be assigned to this class based on temperature properties (e.g. elevated temperature products). Examples of class 9 goods include:
          dry ice, asbestos, and molten bitumen.
      </p>
  </ul>
  <h2>Dangerous Goods Emergency Response </h2>
  <p>
      General procedures for ramp personnel.
  </p>
  <p>
      Airport authorities and operators must have established emergency procedure that incorporate provisions for the handling of spills of dangerous goods or other unidentified substances. The applicable local and company emergency response procedures and
      the relevant emergency contact telephone number must be made available to all employees.
  </p>
  <p>
      General emergency procedure in the event of spill or leakage involving dangerous goods are as follows:
  </p>
  <ul>
      <li>Notify immediate supervisor first; </li>
      <li>Identify the dangerous goods (if safe to do so); </li>
      <li>Where safe to do so, isolate the package by removing other packages or property; </li>
      <li>Avoid contact with the content of the package; </li>
      <li>If the content come in contact with your body or clothes: </li>
      <ul>
          <li>thoroughly wash off body with plenty of water, </li>
          <li>remove contaminated clothing, </li>
          <li>do not eat or smoke, </li>
          <li>keep hands away from eyes, mouth and nose, </li>
          <li>seek medical assistance. </li>
      </ul>
      <li>Staff involved in such incidents should stay on site until their names are noted. </li>
  </ul>
