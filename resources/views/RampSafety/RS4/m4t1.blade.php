  <h2>Accident</h2>
  <p>
    Definitions by Merriam-Webster Online Dictionary :
  </p>
  <ul>
    <li style="background-color: rgb(176, 194, 194)">a sudden event (such as a crash) that is not planned or intended and that causes damage or injury</li>
    <li style="background-color: rgb(176, 194, 194)">an event that is not planned or intended </li>
    <li style="background-color: rgb(176, 194, 194)">an event that occurs by chance</li>
  </ul>
  <br>
  <p>Definitions by Oxford Dictionary</p>
  <ul>
    <li>an unfortunate incident that happens unexpectedly and unintentionally, typically resulting in damage or injury </li>
  </ul>
  <br>
  <p>Aircraft accident:</p>
  <a href="http://www.airtap.umn.edu/publications/factsheets/documents/emergency_man ual_template.pdf">Aircraft Accident Documentation</a>
  <br>
  <p>
    Any occurrence associated with the operation of an aircraft that takes place between the time a person boards the aircraft with the intention of flight and the time such person has disembarked, in which a person suffers death or serious injury as a result of the occurrence or in which the aircraft receives substantial damage.
  </p>
  <br>
  <p>Aircraft Incident:</p>
  <a href="http://www.airtap.umn.edu/publications/factsheets/documents/emergency_man ual_template.pdf">Aircraft Incident Documentation</a>
  <br>
  <p>
    Any occurrence associated with the operation of an aircraft that is not considered an “aircraft accident.” 
  </p>
  <br>
  <h2>Incident Classification System</h2>
  <ol>
    <p>Level 1</p>
    <p>
      An accident may happen. For example, there is a landing gear problem, or a situation or emergency exists or is perceived to exist that may result in an incident or accident. This includes situations where it is unknown if an incident or accident emergency has actually occurred.
    </p>
    <p>Level 2</p><p>
      An aircraft is known or suspected to have an operational defect that affects normal flight operations to the extent that there is danger of an accident. 
    </p>
    <p>Level 3</p>
    <p>
      An aircraft accident has occurred on or in the vicinity of the airport
    </p>
  </ol>
