<div style="text-align: center">
    <h1>FOD Check</h1>
    <hr>
</div>

<div style="text-align: justify">
    <p>
        Prior leaving the ramp area, make sure all of your equipment, tools, foreign objects and other contaminates must be removed promptly and as completely as practicable.
    </p>
    <br>
    <p><b></b></p><img src="https://www.trainairplus.id/draftfile.php/5/user/draft/218250773/FOD%20Check.PNG" alt="" role="presentation"><br>

    <p><br></p>
    <p>
        Clean As You Go:
    </p>
    <ul>
        <li>Clean the immediate area when work cannot continue.</li>
        <li>Clean the immediate area when work debris has the potential to migrate to an out of sight or inaccessible area and cause damage and/or give the appearance of poor workmanship.</li>
        <li>Clean the immediate area after work is completed and prior to inspection.</li>
        <li>Clean at the end of each shift.</li>
        <li>If you drop something or hear something drop -pick it up! </li>
    </ul>
</div>