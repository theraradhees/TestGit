    <h1>Situational Awareness</h1>
    <hr>
</div>
<div style="text-align: justify">
    <p>
        Situational Awareness is being alert to all that is happening around you. Situational Awareness can be affected by a number of things; stress, fatigue, work overload and work under load. The following techniques can help you stay situation aware:
    </p>
    <ul>
        <li>The use of mental pictures;</li>
        <li>Clear and open communication;</li>
        <li>Planning ahead;</li>
        <li>Keeping your eye on the bigger picture;</li>
        <li>Following Standard Operating Procedures.</li>
    </ul>
</div>
