<div class="b">
    <p>
        Hazard identification is a prerequisite to the safety risk management process. Any incorrect differentiation between hazards and safety risks can be a source of confusion. A clear understanding of hazards and their related consequences is essential to the implementation of sound safety risk management.
    </p>
    <p>
        A hazard is generically defined by safety practitioners as a condition or an object with the potential to cause death, injuries to personnel, damage to equipment or structures, loss of material, or reduction of the ability to perform a prescribed function. For the purpose of aviation safety risk management, the term hazard should be focused on those conditions which could cause or contribute to unsafe operation of aircraft or aviation safety-related equipment, products and services.
    </p>
    <p>
        Consider, for example, a fifteen-knot wind, which is not necessarily a hazardous condition. In fact, a fifteen-knot wind blowing directly down the runway improves aircraft take-off and landing performance. However, a fifteen-knot wind blowing in a direction ninety degrees across a runway of intended take-off or landing creates a crosswind condition that may be hazardous due to its potential to contribute to an aircraft operational occurrence, such as a lateral runway excursion.
    </p>
    <p>
        Hazards are an inevitable part of aviation activities. However, their manifestation and possible consequences can be addressed through various mitigation strategies to contain the potential for a hazard to result in unsafe aircraft or aviation equipment operations.
    </p>
    <p>
        There is a common tendency to confuse hazards with their consequences or outcomes. A consequence is an outcome that can be triggered by a hazard. For example, a runway excursion (overrun) is a projected consequence in relation to the hazard of a contaminated runway. By first defining the hazard clearly, one can then project the proper consequence or outcome. It may be noted that consequences can be multi-layered, including such things as an intermediate unsafe event before an ultimate consequence (accident).
    </p>
    <p>
        In the crosswind example above, an immediate outcome of the hazard could be loss of lateral control followed by a consequent runway excursion. The ultimate consequence could be an accident. The damaging potential of a hazard materializes through one or many consequences.
    </p>
    <p>
        It is therefore important for safety assessments to include a comprehensive account of all likely consequences, described accurately and in practical terms. The most extreme consequence, loss of human life, should be differentiated from those that involve the potential for lesser consequences such as increased flight crew workload, passenger discomfort or reduction in safety margins.
    </p>
    <p>
        The description of consequences according to their plausible outcomes will facilitate the development and implementation of effective mitigation strategies through proper prioritization and allocation of limited resources. Proper hazard identification leads to appropriate evaluation of their potential outcomes.
    </p>
    <p>
        Hazards should be differentiated from error, a normal and unavoidable component of human performance, which must be managed.
    </p>
    <p>
        Whenever a hazard is identified, follow these steps:
    </p>
    <ul>
        <li>Remove the hazard if you can.</li>
        <li>Warn others if you cannot remove it.</li>
        <li>Record the hazard and report it to appropriate parties.</li>
    </ul>
