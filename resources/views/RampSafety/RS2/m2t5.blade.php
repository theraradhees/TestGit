<div style="text-align: center">
    <h1>Safety Driving Measures</h1>
    <hr>
</div>
<div style="text-align: justify">
    <p>
        Anyone driving a vehicle airside (without a follow-me) is required to hold an airside driving license. The use of vehicles airside is to be kept to the minimum consistent with operational requirements.    
    </p>
    <p>
        When traveling on the apron, always use designated vehicle service roads. Driving close to buildings, around vehicles, or aircraft is prohibited. This policy helps to establish a predictable order to vehicle movements in congested areas and helps to ensure their visibility to aircraft and other vehicles.
    </p>
    <p>
        The intent of operational requirements and procedures is to ensure the safe and orderly movement of passengers, aircraft and vehicular traffic.
    </p>
    <p>
        The requirements and procedures of driving on apron are:
    </p>
    <ol>
        <li>Speed Limits</li>
        <p>
            Drivers shall obey all regulatory signs in the airside and adhere to the speed limits of: 
        </p>
        <ul>
            <li>5 km per hour within 10m of an aircraft; </li>
            <li>25  km  per hour  on  roadways  within  15  meters  of  an  aircraft stand; and </li>
            <li>40  km  per  hour  on  roadways  situated  more  than  15  meters away from any aircraft stand.</li>
            <li>10 km per hour in the Baggage Handling Area.</li>
        </ul>
        <li>When Aircraft is Stationary with Engines Running</li>
        <p>
            Drivers  and  airside  personnel  must  be  aware  of the  dangerous effects of jet blast injuries that could be caused by rotating propellers and  potential  ingestion  when  in  close  proximity  to  aircraft  with  its engine running.
        </p>
        <ul>
            <li>They  shall  not  cross  in  front  or  behind  a  taxiing  aircraft  that would subject themselves and or aircraft in danger.</li>
            <li>No driver shall drive a vehicle towards an aircraft with its engine running. An aircraft with its engine running will have its anti-collision light on (displaying a flashing red light signal)</li>
            <li>Any person except maintenance staff working on a stationary aircraft with its engine running:</li>
            <ul>
                <li>shall  not  pass  within  8 meters from  the air  intake of  the aircraft engine.</li>
                <li>shall not pass within 80 meters from the rear of an aircraft jet engine</li>
                <li>must be at least 5 meters clear from the wing tip of a parked aircraft</li>
            </ul>
        </ul>
        <li>When Aircraft is Taxiing</li>
        <p>
            Personnel and vehicles shall give way to aircraft at all times irrespective of whether the aircraft is taxiing or on tow. The drivers should keep a distance of 40 meters from the taxiway edge when the vehicle runs parallel taxiway when aircraft is taxiing at taxiway.
        </p>
        <li>
            When Aircraft Being Refueled
        </li>
        <ul>
            <li>Drivers shall not start their vehicles when within 15 meters of a refueling aircraft.</li>
            <li>Drivers of vehicles shall keep clear of the aircraft engines and shall no pass within 2.5 meters radius around the aircraft fuel tank vents.</li>
            <li>Drivers  of  vehicles  shall  not  drive  over  any  hose  or  bonding cable laid during aircraft refueling.</li>
        </ul>

    </ol>
</div>
