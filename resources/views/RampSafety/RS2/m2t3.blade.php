    <h1>Risk Management</h1><hr>
</div>
<div class="b">
    <p>
        Risk management is the identification, analysis and elimination (and/or mitigation to an acceptable or tolerable level) of those hazards, as well as the subsequent risks, that threaten the viability of an organization.‚Äù (ICAO Doc 9859).
    </p>
    <p>
        The complete elimination of risk in aviation operations obviously is an unachievable and impractical goal (being perfectly safe means to stop all aviation activities and to ground all aircraft). As not all risks can be removed, nor are all possible risk mitigation measures economically practical. In other words, it is accepted that there will be some residual risk of harm to people, property or environment, but this is considered to be acceptable or tolerable by the responsible authority and the society.
    </p>
    <p>
        Risk management, being a central component of the SMS, plays vital role in addressing the risk in practical terms. It requires a coherent and consistent process of objective analysis, in particular for evaluating the operational risks. In general, Risk Management is a structured approach and systematic actions aimed to achieve the balance between the identified and assessed risk and practicable risk mitigation.
    </p>
    <p>
        Risk management consists of three essential elements:
    </p>
   <ol>
       <li>Hazard identification</li>
       <p>
        Identification  of  undesired  or  adverse  events  that  can  lead  to  the occurrence  of  a  hazard  and  the  analysis  of  mechanisms  by  which these  events  may  occur  and  cause  harm.  Both  reactive  and proactive  methods  and  techniques  should  be  used  for  hazard identification.
       </p>
       <li>
        Risk Assessment
       </li>
       <p>
        Risk is the probability and severity of loss from exposure to the hazard. Identified hazards are assessed in terms of criticality of their harmful effect and ranked in order of their risk-bearing potential. They are assessed often by experienced personnel, or by utilizing more formal techniques and through analytical expertise. The severity of consequences and the likelihood (frequency) of occurrence of hazards are determined. If the risk is considered acceptable, operation continues without any intervention, if it is not acceptable, risk mitigation process is engaged.
       </p>
