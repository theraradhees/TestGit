    <h2>Aerodrome Marking and Sign</h2>
    <p>
        There are markings and signs used at aerodromes, which provide directions and assist pilots and ramp operation personnel (especially drivers)in aerodrome operations.
    </p>
    <h3>Marking</h3>
    <p>
        Marking is a symbol or group of symbols displayed on the surface of the movement area in order to convey aero-nautical information.
    </p>
    <p>
        Different markings are used to identify different areas of the aerodrome. All airside users must be familiar with the airside markings. There are 3 (three) common markings are discussed.
    </p>
    <p>
        Some of the most common markings and signs are discussed.
    </p>
    <ol>
        <li>Runway Markings</li>
        <p>
            Runway markings shall be white. Runway markings vary depending on the type of operations conducted at the airport. Figure 1-2shows a runway that is approved as a precision instrument approach runway and some other common runway markings.
        </p>
        <p>
            Since aircraft are affected by the wind during takeoffs and landings, runways are laid out according to the local prevailing winds. Runway numbers are in reference to magnetic north. Certain airports have two or even three runways laid out in the same
            direction. These are referred to as parallel runways and are distinguished by a letter added to the runway number (e.g., runway 36L (left), 36C (center), and 36R (right)).
        </p>
        <p style="text-align: center;"><span style="font-size: 13.6px;"><img src="https://www.trainairplus.id/draftfile.php/5/user/draft/61293983/Figure%201-2.%20Selected%20airport%20markings%20and%20surface%20lighting.PNG" alt="" role="presentation"><br></span></p>
        <p style="text-align: center">
            <b>Figure 1-2.Selected airport markings and surface lighting</b>
        </p>
        <p style="text-align: center"><b>Source: The Pilot’s Handbook of Aeronautical Knowledge, FAA, 2008</b></p>
        <br>

        <li>Taxiway Markings</li>
        <p>
            Aircraft use taxiways to transition from parking areas to the runway. Taxiways are identified by a continuous yellow center line stripe and may include edge markings to define the edge of the taxiway. This is usually done when the taxiway edge does not
            correspond with the edge of the pavement. If an edge marking is a continuous line, the paved shoulder is not intended to be used by an aircraft.
        </p>
        <p>
            If it is a dashed marking, an aircraft may use that portion of the pavement. Where a taxiway approaches a runway, there may be a holding position marker. These consist of four yellow lines (two solid and two dashed). The solid lines are where the aircraft
            is to hold. At some towered airports, holding position markings may be found on a runway.
        </p>

        <p>
            Something to remember:
        </p>
        <ul>
            <li>
                It is always permissible to cross from the dashed side to the solid side
            </li>
            <li>ATC permission is always required to cross from the solid side to the dashed side at an aerodrome with an operating control tower</li>
            <li>
                When instructed to “Hold Short” always stop prior to first solid line of the runway holding position marking.
            </li>
