        <p>
            Some other markings found on the airport include vehicle roadway markings, VOR receiver checkpoint markings, and non-movement area boundary markings.
        </p>
        <p>
            Vehicle roadway markings are used when necessary to define a pathway for vehicle crossing areas that are also intended for aircraft. These markings usually consist of a solid white line to delineate each edge of the roadway and a dashed line to separate
            lanes within the edges of the roadway. In lieu of the solid lines, zipper markings may be used to delineate the edges of the vehicle roadway. [Figure 1-4]
