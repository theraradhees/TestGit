    <h3>Lighting</h3>
    <ol>
        <li>Runway Light</li>
        <p>
            All runways licensed for night use must have lighting which at least defines the extent of the runway. This is referred to as Edge Lighting, Threshold Lighting and Runway End Lighting. Other types of lighting may also be provided.
        </p>
        <p>
            ICAO guidance requires that Runway lighting shall not be operated if a runway is not in use for landing, take-off or taxiing purposes, unless such operation is required for runway inspection or maintenance purposes. ATC are required to use whatever means
            are available to them to ensure that they are aware of any lighting system unserviceability so that appropriate notification action can be taken.
        </p>
        <p>
            There are various lights that identify parts of the runway complex. Minimum Runway Lighting are:
        </p>
        <ul>
            <li>Runway Edge Lights are Omni-directional and are located along or just beyond the edges of the area declared for use as the runway as defined by edge markings and are white subject to certain specific exceptions. The area defined may not necessarily
                be the maximum width of the paved runway surface. The lights may be either elevated or embedded in the surface. If a landing threshold is displaced, but the pre-landing threshold area is available for takeoff, then the edge lights between
                the beginning of the runway surface and the displaced threshold will be split so as to show red up to the landing threshold whilst still showing white after that point. If a runway ‘starter extension’ is provided which is narrower than
                its associated runway, then blue edge lighting may be used to mark its edges.</li>
            <li>
                Runway Threshold Lights are provided in a line along the landing threshold at the touchdown end of a runway and define the beginning of the declared Landing Distances. They are green and can only be seen from the approach.
            </li>
            <li>
                Runway End Lights are provided in a line along the end of the runway available for use. They are red and can only be seen in the direction of runway use.
            </li>
        </ul>
        <p></p>
        <li>
            Taxiway Light
        </li>
        <p>
            Omnidirectional taxiway lights outline the edges of the taxiway and are blue in color. At many airports, these edge lights may have variable intensity settings that may be adjusted by an air traffic controller when deemed necessary or when requested by
            the pilot. Some airports also have taxiway centerline lights that are green in color.
        </p>
    </ol>
