    <ol>
        <li>Establishment and continuation of communications </li>
        <ul>
            <li>When establishing communications, a vehicle should use the full call sign of both the vehicle; and the aeronautical station. When a ground station wishes to broadcast information, the message should be prefaced by the call “ALL STATIONS”.</li>
            <li>When a vehicle wishes to broadcast information to other vehicles, the message should be prefaced by the call “ALL STATIONS”. </li>
        </ul>
        <li>No reply is expected to such general calls unless individual stations are subsequently called upon to acknowledge receipt. </li>
        <ul>
            <li>If there is doubt that a message has been correctly received, a repetition of the message shall be requested either in full or in part.</li>
        </ul>
