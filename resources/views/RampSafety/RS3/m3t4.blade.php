        <p>Your response, straight away : “Vacating Runway 32, Car 1” </p>
        <br>
        <p>Once you have vacated and are outside the runway strips, you call the Tower: </p>
        <p>You : “Car 1 Vacated Runway 32” </p>
        <p>The Tower will acknowledge : “Tower” </p>
