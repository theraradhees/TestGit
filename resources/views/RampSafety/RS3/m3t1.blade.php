<h2>Coordination with Air Traffic Control Units </h2>
<h3>General Movement </h3>
<ol>
    <li>
        The expeditious movement of vehicles plays an essential supporting role in the operation of an aerodrome. Wherever possible the areas in which vehicles and aircraft operate are segregated. There are, however, many occasions when vehicles need to move on the maneuvering area for maintenance purposes or in direct support of aircraft operations. 
    </li>
    <li>
        Procedures governing the movement of vehicles vary widely from aerodrome to aerodrome, but certain factors to be taken into account when driving on an aerodrome are common to all:
    </li>
    <ul>
        <li>Aircraft are not as maneuverable as ground vehicles;</li>
        <li>The visibility from an aircraft cockpit for ground movement purposes is often restricted compared to that from a ground vehicle </li>
    </ul>
    <p>
        Therefore, when vehicles are operating in close proximity to aircraft, drivers should be extremely vigilant and comply in full with local procedures and ATC instructions. 
    </p>
    <ul>
        <li>Correct RTF operating techniques must be observed by all users. It is important that a continuous listening watch is maintained by all drivers on the movement area, not only in case of further instructions from the control tower, but also so that drivers can be aware of the movements, and intended movements, of other traffic, thereby reducing the risk of conflict. </li>
    </ul>
</ol>
<h2>Coordination with Ground Station Manager</h2>
<p>
    Procedures governing the movement of vehicles vary widely from aerodrome to aerodrome, therefore consider to coordinate with the Station Manager for the instruction.
</p>