    <h1>Situation Requiring Pre-entry Permission </h1>
    <hr>
</div>
<div class="b">
    <ol>
        <li>Before Go Out onto the Movement Area </li>
        <ul>
            <li> KNOW THE PROCEDURES</li>
            <li> KNOW THE LIGHT SIGNALS</li>
            <li> BE PRECISE – AND PATIENT</li>
            <li> COMPLY WITH HANDBOOK</li>
            <li> KEEP YOUR EYES OPEN, STAY ALERT AND NEVER GO BEYOND HEARING RANGE OF YOUR RADIO </li>
            <li> PLAN WORK CAREFULLY AND AVOID ANY TENDENCY TO RUSH WHILST AIRSIDE</li>
            <li> NEVER LEAVE ANYTHING (EQUIPMENT OR TOOLS) ON THE MOVEMENT AREA </li>
        </ul>
        <p>
            Note: If you become confused about what is happening, leave the movement area and consult someone about further training. 
        </p>
        <li>Light Signals</li>
        <p>
            If you received light signals from the Tower, respond to them promptly. The meaning of these signals may be displayed on the left hand corner of your Vehicle windscreen. 
        </p>
        <p>
            Signals and their meaning are listed hereunder: 
        </p>
        <ul>
            <li>GREEN FLASHES - Permission to cross runway or to move on a Taxiway</li>
            <li>STEADY RED - Stop immediately </li>
            <li>RED FLASHES - Move off the runway or taxiway and watch out for aircraft</li>
            <li>WHITE FLASHES - Vacate the Maneuvering Area in accordance with local Handbook </li>
        </ul>

        <li>The meaning of “HOLD”</li>
        <p>
            The word "stop" is rarely used in radio transmissions from the Tower - instead you will hear the word "HOLD", which means "STOP". 
        </p>
        <p>
            Examples of the use of "HOLD" are: 
        </p>
        <p>
            "HOLD YOUR POSITION" (Stop where you are) "HOLD SHORT OF RUNWAY ZERO ONE" (Stop clear of the runway strip) 
        </p>
        <li>Entering a Runway </li>
        <p>
            Example:
        </p>
        <p>"NADIM GROUND THIS IS SAFETY ONE ON DOMESTIC APRON, TO ENTER TAXIWAY BRAVO AND RUNWAY ONE ONE REMAINING CLEAR OF RUNWAY TWO ONE, ON IMMEDIATE RECALL" </p>
        <br>
        <p>
            (Tower response: "SAFETY ONE, ENTER BRAVO AND RUNWAY 11") (Your acknowledgement: "ENTERING BRAVO AND RUNWAY ONE ONE – SAFETY ONE") 
        </p>

        <li>Entering the Maneuvering Area from Apron </li>
        <p>Example:</p>
        <p>
            "NADIM GROUND - TRUCK ELEVEN AT SOUTHERN END OF DOMESTIC APRON - TO ENTER RUNWAY ZERO ONE / TAXIWAY MIKE INTERSECTION VIA TAXIWAY ALPHA - ESTIMATED DURATION FIVE MINUTES" (Tower response: "TRUCK 11, ENTER INTERSECTION RUNWAY 01 AND TAXIWAY MIKE")
        </p>
        <p>
            (Your acknowledgement: "ENTERING INTERSECTION RUNWAY ZERO ONE AND TAXIWAY MIKE - TRUCK ELEVEN") 
        </p>
        <li>Crossing a Runway</li>
        <p>Example:</p>
        <p>
            "NADIM GROUND - CAR FOURTEEN ON TAXIWAY BRAVO - TO CROSS RUNWAY ZERO SEVEN"
        </p>
        <p>(Tower response: "CAR 14 HOLD SHORT OF RUNWAY 07") </p>
        <p>(Your acknowledgement: "HOLDING SHORT OF RUNWAY ZERO SEVEN - CAR FOURTEEN")</p>
        <br>
        <p>
            Note: Your request was denied at this time and would be followed, when the runway is clear, by "Car 14, cross Runway 07 
        </p>

        <li>Vacating onto other Runways </li>
        <p>
            Before crossing or entering another runway whilst vacating an active runway, you must request and obtain a clearance to do so.
        </p>
        <p>Example:</p>
        <p>
            (Tower: "CAR 4 - VACATE RUNWAY 17") Car 4: "VACATING RUNWAY ONE SEVEN - REQUEST TO VACATE ONTO RUNWAY ZERO EIGHT TILL ONE SEVEN AVAILABLE - CAR FOUR" (Tower: "CAR 4 - NEGATIVE - ENTER TAXIWAY BRAVO") 
        </p>
        <p>
            (Your acknowledgement: "ENTERING TAXIWAY BRAVO - CAR FOUR") Clearly neither runway is available and you are to immediately enter Taxiway B and clear the runway strip markers. 
        </p>
        <li>Typical Replies from the Tower Advising of Restrictions </li>
        <p>Example:</p>
        <p>
            "CAR TWO - NADIM GROUND - HOLD POSITION" (Stay where you are and await further details regardless of where you are) "CAR THREE - NADIM GROUND - ENTER RUNWAY ZERO THREE - CROSS RUNWAY ZERO SIX - EXPEDITE" 
        </p>
        <p>(Cross Runway 06 without any delay and enter Runway 03. You must not enter Runway 06 again without approval) </p>
        <br>
        <p>
            "CAR NINE - NADIM GROUND - HOLD POSITION - EXPECT ONE ZERO MINUTE DELAY" (Several aircraft are on approach or taxying for departure - you may wish to try later) "TRUCK SIXTEEN - VACATE RUNWAY ZERO NINE"
        </p>
        <p>(Regardless of what you are doing, what you have requested or what you have been cleared to do so far, move outside the Maneuvering Area, clear of the runway strip markers). </p>

    </ol>