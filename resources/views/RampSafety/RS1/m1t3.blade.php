<h2>Hand Protection</h2>
<p>
    Select and use the appropriate hand protection when employees' hands may be potentially exposed to the following hazard sources: 
</p>
<ul>
    <li>Skin absorption of harmful substances </li>
    <li>Severe cuts or lacerations </li>
    <li>Severe abrasions </li>
    <li>Punctures</li>
    <li>Chemical burns</li>
    <li>Irritating materials</li>
    <li>Thermal burns</li>
    <li>Harmful temperature extremes</li>
</ul>
<p>
    It is important to select appropriate gloves (as shown in figure 1.12) for a particular application and to determine how long the glove can be worn, and whether it can be reused. Cloth gloves must not be worn when operating rotating equipment such as a drill or powered threading machine. 
</p>
<br>
<p><b>Figure 1.12 Type of Gloves: a) Metal Mesh Gloves, b) Vinyl and neoprene gloves, c) Leather gloves, d) Rubber Gloves, e) Padded cloth gloves, f) Heat resistant gloves, g) Latex disposable gloves, h) Lead-lined gloves </b></p>
<br>
<p>
    Type of hand protection i.e. 
</p>
<ul>
    <li>Gloves</li>
    <ul>
        <li>Metal mesh gloves: resist sharp edges and prevent cuts; </li>
        <li>Leather gloves: shield your hands from rough surfaces; </li>
        <i>Vinyl and neoprene gloves: protect your hands against toxic chemicals; </i>
        <li>Rubber gloves: protect you when working around electricity; </li>
        <li>Padded cloth gloves: protect your hands from sharp edges, slivers, dirt, and vibration; </li>
        <li>Heat resistant gloves: protect your hands from heat and flames;</li>
        <li>Latex disposable gloves: used to protect your hands from germs and bacteria; </li>
        <li>Lead-lined gloves: used to protect your hands from radiation sources. </li>
    </ul>
</ul>
