<h2>Foot Protection</h2>
<p>
    Select protective footwear when employees work in areas where there is a danger of foot injuries due to falling and rolling objects, objects piercing the sole, and where employees' feet are exposed to electrical hazards. Examples of situations that may require the use of protective footwear include: 
</p>
<ul>
    <li>Handling heavy objects and/or tools that could be dropped; </li>
    <li>Work activities involving manual material handling carts, heavy pipes, or bulk rolls, all of which could potentially roll over an employee's feet; </li>
    <li>Work involving sharp objects such as nails, tacks, large staples, scrap metal, etc., which could penetrate the sole of the shoe;</li>
    <li>Work involving explosive materials such as black powder, volatile substances, cotton dust, grain dust that could be ignited by the discharge of static electricity; </li>
    <li>Work with electrical hazards; </li>
    <li>Work with electronic components. </li>
</ul>
