<h2>
    Appreciate Need for Having PPE
</h2>
<p>
    Hazards exist in every workplace in many different forms: sharp edges, falling objects, flying sparks, chemicals, noise and a myriad of other potentially dangerous situations. When an injury occurs in the workplace, not only does the worker suffer but the costs-medical and time loss payments, loss in productivity, costs for a replacement worker, and potential increase in insurance premium costs can be great. Personal protective equipment must be selected to protect against any hazard that is present or likely to be present. It is important for the workers to become familiar with the potential hazards, the type of protective equipment that is available, and the level of protection that is provided by that equipment, i.e., splash protection, impact protection, etc. 
</p>
<br>
<h2>Eye and Face Protection </h2>
<p>
    Eye and face protection must be used where a hazard exists due to any of the following: 
</p>
<ul>
    <li>Flying Objects or Particles;</li>
    <li>Molten Metal;</li>
    <li>Liquid chemicals;</li>
    <li>Harmful contacts;</li>
    <li>Exposures;</li>
    <li>Acids or caustic chemicals; </li>
    <li>Chemical gases or vapors; </li>
    <li>Glare;</li>
    <li>Air Contaminants;</li>
    <li>Radiation;</li>
    <li>Electrical flash;</li>
</ul>
