<h2>Body Prote4ction</h2>
<p>
    Body protection should be worn when there is a possibility of bodily injury from hazards while performing their jobs. 
</p>
<p>
    Workplace hazards include the following:
</p>
<ul>
    <li>Intense Heat</li>
    <li>Splashes of hot metals and other hot liquids </li>
    <li>
        Impacts from tools, machinery and materials
    </li>
    <li>Cuts</li>
    <li>Hazardous chemicals</li>
    <li>contact with potentially infectious materials, like blood(refer to the Biosafety Manual for PPE guidelines)</li>
    <li>Radiant energy (ultraviolet, visible, infrared)</li>
    <i>Motor vehicle traffic</i>
</ul>
<p>
    Protective clothing needs to be provided only for the specific parts of the body that are exposed to the hazard. Depending on the hazards of the workplace one or more of the following may need to be provided: 
</p>
<ul>
    <li>Vests</li>
    <li>Jackets</li>
    <li>Aprons</li>
    <li>Coveralls</li>
    <li>Sleeve protectors </li>
    <li>Surgical gowns</li>
    <li>Full body suits</li>
</ul>
<br>
<h2>Serviceability of Safety Equipment </h2>
<p>
    The personal protective equipment selected must fit the employee it is intended to protect. Make certain that employees have the correct size of protective equipment. Whenever possible, select adjustable personal protective equipment. Employee input in the selection process is critical. Personal protective equipment hat fits properly and is comfortable will more likely be worn by employees. Damaged or defective protective equipment must be taken out of service immediately to be repaired or replaced and employees must be provided with the proper equipment in the interim. 
</p>
<p>
    As appropriate, ramp personnel should wear safety footwear, gloves, knee pads. High visibility clothing, toilet-servicing and wet and winter weather gear, sun hats, sunglasses, earmuffs, and sunscreen. High visibility clothing should be mandatory on the ramp. Clothing should fit snugly to prevent becoming snagged. 
</p>

