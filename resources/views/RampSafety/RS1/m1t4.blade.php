<div class="a">
    <h1>Safety Preparation for Vehicle</h1><hr>
</div>

<div class="b">
    <P>
        Airside driver has responsible to ensure that the vehicle are roadworthy before driving, they should conduct safety preparation i.e.: 
    </P>
    <UL>
        <li>Check that the vehicle permit still valid; </li>
        <li>Check that all equipment on vehicle are work properly (light, brake, etc.);</li>
        <li>Check that there are no leakages on fuel and oil tank; </li>
        <li>Check that there are no leakages from combustion chamber; </li>
        <li>
            Check that the vehicle equipped with fire extinguisher placed safely, easy to reach and ready to operate ; 
        </li>
        <li>Check that the safety belt are work properly;</li>
        <li>There is flame trap for vehicle with the fuel other than solar (diesel). </li>
    </UL>
</div>
