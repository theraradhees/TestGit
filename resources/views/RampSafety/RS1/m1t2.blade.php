<div class="b">
    <p>
        Evaluation and the development of an organization’s safety culture is the next step in the improvement of aviation safety. The full advantage and benefits of safety improvement programs are assured and secured in a sound and mature safety culture.
    </p>
    <p>
        There is a trend for safety culture to be expressed in terms of attitudes or behavior. Glendon et al., (2006, p. 367) highlight that when defining safety culture the premise of some researchers is to focus on attitudes, where others emphasize safety culture being expressed through their behavior and work activities. In other words, the safety culture of an organization acts as a guide as to how employees will behave in the workplace. Of course their behavior will be influenced or determined by what behaviors are rewarded and acceptable within the workplace. For example, Clarke (2006, p. 278) states that the safety culture is not only observed within the “general state of the premises and conditions of the machinery but in the attitudes and behaviors of the employees towards safety”.
    </p>
    <p>
        It is important to identify the perception of the organization’s safety culture as it represents a critical factor influencing multiple aspects of human performance and organizational safety. One of the most succinct and usable definitions of safety culture can be found in von Thaden and Gibbons (2008): Safety culture is defined as the enduring value and prioritization of worker and public safety by each member of each group and in every level of an organization. It refers to the extent to which individuals and groups will commit to personal responsibility for safety; act to preserve, enhance and communicate safety concerns; strive to actively learn, adapt and modify (both individual and organizational) behavior based on lessons learned from mistakes; and strive to be honored in association with these values. 
    </p>
    <p>
        This definition combines key issues such as personal commitment, responsibility, communication, and learning in ways that are strongly influenced by upper-level management, but include the behaviors of everyone in the organization. It implies that organizations possess a safety culture of some sort, but this culture is expressed with varying degrees of quality and follow-through. 
    </p>
